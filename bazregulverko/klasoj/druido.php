<p id="konekto"><b>Konekto (EE):</b> La druido povas interrilati kun la kunulo per libera ago kaj povas instigi ĝîn per movago eĉ se li ne havas rangon en interrilati kun bestoj. La druido ricevas situaciobonon +4 por ĵetoj de bestkunsento kaj interrilati kun bestoj por la kunulo. </p>

<p id="kunpartigi-sorcxojn"><b>Kunpartigi sorĉojn (EE):</b> La druido povas efektivigi sorĉojn kiujn li kutime nur rajtus efektivigi al si mem ankaŭ al la kunulon (kiel sorĉo kun la trafopovo tuŝo). Tiuj ankaŭ povas esti sorĉoj kiuj kutime ne aplikeblas al bestoj. Sorĉojn kiuj estas efektivigitaj tiamaniere devas deveni de klaso kiu havas bestkunulon. Tiu ebleco ne permesas kunpartigi ecojn kun la bestkunulo eĉ se ili efektiviĝas kiel sorĉoj. </p>

<p id="sinsavi"><b>Sinsavi (EE):</b> Se la kunulo estas celo de atako kiu permesas duonan damaĝon ĉe sukcesa refleksĵeto, la kunulo ricevas neniun damaĝon ĉe sukcesa savĵeto. </p>

<p id="altigxo-de-atributvaloro"><b>Altiĝo de atributvaloro (EE):</b> La bestkunulo povas aldoni +1 al unu el siaj atributoj. </p>

<p id="sindedicxo"><b>Sindediĉo (EE):</b> La kunulo ricevas moralbonon +4 por voloĵetoj kontraŭ transsorĉoj kaj transsorĉaj efikoj. </p>

<p id="multobla-atako"><b>Multobla atako:</b> La kunulo ricevas, se li ne jam havas, multoblan atakon kiel kroman talenton, se li havas tri aŭ pli naturajn atakojn. Se li ne havas tri naturajn atakojn li ricevas aldonan atakon per sia natura armilo kun malbono -5. </p>

<p id="sinsavi-progresita"><b>Sinsavi progresita (EE):</b> Se la kunulo estas la celo de atako kiu permesas duonan damaĝon ĉe sukcesa refleksĵeto, la kunulo ricevas neniun damaĝon ĉe sukcesa savĵeto kaj duonan damaĝon ĉe malsukceso. </p>


