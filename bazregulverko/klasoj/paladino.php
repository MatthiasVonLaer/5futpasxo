<h2>Klasaj karakterizaĵoj</h2>

<p> La sekvantaj eblecoj estas la klasaj karakterizaĵoj de la paladino. </p>

<p><b>Sperto kun armiloj kaj armaĵoj:</b> La paladino havas sperton kun ĉiuj simplaj kaj militaj armiloj. Li estas sperta kun ĉiuj armaĵoj kaj ŝildoj krom turŝildoj.</p>

<p id="auxro-de-bono"><b>Aŭro de bono (EE):</b> La forto de la aŭro de bono (vidu la sorĉon malkovri bonon) de la paladino egalas lian nivelon en tiu ĉi klaso. </p>

<p id="malkovri-malbonon"><b>Malkovri malbonon (SSE):</b> La paladino povas uzi tiun eblecon ĉiam kiam li volas kiel la sorĉo malkovri malbonon. Per movago li povas koncentriĝi al sola objekto aŭ individuo en la ĉirkaŭaĵo de 18m kaj malkovri ĉu la celo estas malbona kaj eltrovi la forton de ĝia aŭro tiel kiel li studus la celon 3 rondojn. Kiam la paladino koncentriĝas al unu celo, li ne povas eltrovi la malbonan pensmanieron en aliaj estaĵoj aŭ objektoj. </p>

<p id="pereigi-malbonon"><b>Pereigi malbonon (SNE):</b> Unufoje tage la paladino povas voki la fortojn de la bono por ke ili estu kun li en la batalo kontraŭ la malbono. Per rapida ago li elektas videblan celon por pereigi. Se la celo havas malbonan pensmanieron, li aldonas sian KAR-bonon (se li havas) al ĉiuj atakĵetoj kaj aldonas sian paladinonivelon al la damaĝon kiun li kaŭzas al la celo per pereigi malbonon. Se la celo de pereigi malbonon eĉ estas eksterulo de la subkategorio malbono, drako kun malbona pensmaniero aŭ malmortulo, la kroma damaĝo por la unua sukcesa atako altiĝas al 2 poentoj por ĉiu paladinonivelo. Sendepende de la celo la atakoj de pereigi malbonon preteriras ĉiun damaĝoredukton de la kreaĵo. </p>
<p> Krome la paladino ricevas distran bonon, kiu egalas al lia KAR-bono (se li havas), al sia armaĵklaso kontraŭ atakoj de la celo de sia pereigi malbonon. Se la paladino elektas malamikon kiel celo kiu ne estas malbona, la pereigi malbonon nuliĝas sen efiko. </p>
<p> La efikoj de pereigi malbonon daŭras tiom longe ĝis la celo estas morta aŭ la paladino faris sufiĉe longan ripozon kaj reakiris la eblecon por pereigi malbonon denove. Je la 4a nivelo kaj por ĉiu plia tria nivelo poste, la paladino povas uzi la eblecon unu plian fojon ĉiutage ĝis maksimumo de sep fojoj je la 19a nivelo kiel estas indikita en la supra tabelo. </p>

<p id="dia-graco"><b>Dia graco (SNE):</b> Je la 2a nivelo la paladino aldonas sian KAR-bonon (se li havas) al ĉiuj savĵetoj. </p>

<p id="surmeti-manon"><b>Surmeti manon (SNE):</b> Ekde la 2a nivelo la paladino povas sanigi vundojn per tuŝo al si mem aŭ al aliancanoj. Li povas uzi tiun eblecon tiom ofte kiel egalas lia duona paladinonivelo plus lia KAR-modifilo. Per unu aplikado li povas sanigi 1k6 vivpoentojn por ĉiuj 2 paladinoniveloj. Por uzi tiun eblecon li devas uzi unu bazagon krom li mem estas la celo. Tiukaze tio nur estas rapida ago. La paladino nur bezonas unu liberan manon por surmeti manon. </p>
<p>Alternative la paladino povas uzi surmeti manon por lezi malmortulojn anstataŭ sanigi kaj kaŭzas 1k6 damaĝon por ĉiuj du paladinoniveloj. Por uzi la eblecon tiamaniere la paladino devas sukcese fari tuŝatakon. Tiu atako ne provokas okazatakon. Malmortuloj ne havas savĵeton kontraŭ tiun damaĝon. </p>

<p id="auxro-de-braveco"><b>Aŭro de braveco (SNE):</b> Je la 3a nivelo la paladino îgas imuna kontraŭ ĉiu tipo de timo ĉu de natura ĉu de magia deveno. Aldone ĉiu aliancano en la ĉirkaŭaĵo de 3 metroj ĉirkaŭ la paladino ricevas moralbonon +4 por siaj savĵetoj kontraŭ timo. Tiu ebleco nur funkcias se la paladino estas konscienca. Se li estas senkonscia aŭ morta li ne plu povas doni bonon al aliancanoj. </p>

<p id="dia-saneco"><b>Dia saneco (EE):</b> Je la 3a nivelo la paladino iĝas imuna kontraŭ ĉiuj malsanecoj eĉ se ili estas de supernatura aŭ magia deveo. </p>

<p id="beno"><b>Beno (SNE):</b> Je la 3a nivelo kaj je ĉiu plia tria nivelo la paladino povas elekti unu benon. Ĉiu beno aldonas efikon al surmeti manon. Ĉiam kiam la paladino uzas surmeti manon por sanigi damaĝon de la celo, la celo ankaŭ ricevas la kromajn efikojn de ĉiuj benoj kiuj estas donitaj al paladino. Beno povas malaperigi staton kiu estas kaŭzita de malbeno, malsano aŭ de veneno sen sanigi la celon de la kaŭzo mem. Tiuj statoj reaperos post unu horo se la kaŭzo mem ne estos sanigita. </p>


