<h2>Klasaj karakterizaĵoj</h2>

<p> La sekvantaj eblecoj estas la klasaj karakterizaĵoj de la magiisto. </p>

<p><b>Sperto kun armiloj kaj armaĵoj:</b> La magiisto havas sperton kun la arbalestoj (<a href="../ekipajxo/#arbalesto-legxera">malpeza</a> kaj <a href="../ekipajxo/#arbalesto-peza">peza</a>), ... Ĉiu speco de armaĵo malhelpas la moveblon de la magiisto kio povas kaŭzi ke sorĉoj kun gesta komponanto fuŝas (vidu arkanaj sorĉoj kaj armaĵoj). </p>

<p><b>Sorĉoj:</b> La magiisto scipovas efektivigi arkanajn sorĉojn kiujn li elektas de la sorĉlisto por sorĉisto / magiisto. La magiisto devas elekti siajn sorĉojn kaj prepari ilin. Por lerni kaj prepari sorĉon, la magiisto devas havi inteligentecon de minimume 10 + grado de la sorĉo. La malfacilecgrado por la savĵeto kontraŭ la sorĉo de la magiisto estas 10 + grado de la sorĉo + INT-modifilo de la magiisto. </p>
<p> La magiisto nur povas efektivigi certan kvanton da sorĉoj ĉiutage. La nombro da sorĉoj por ĉiu tago estas indikitaj en la supra tabelo. Aldone la magiisto ricevas kromaj sorĉoj ĉiutage se li havas altajn inteligenteco-valorojn (vido paragrafon: <a href="../ekludi/#atributoj-kaj-sorcxkonantoj">Atributoj kaj sorĉkonantoj</a>). </p>
<p> La kvanto da sorĉoj kiujn la magiisto volas lerni ne estas limigita. Li devas prepari la sorĉojn kiujn li volas efektivigi. Post dormo de ok horoj la magiisto devas studi sian sorĉolibron dum unu horo. Dum la studado li elektas la sorĉojn kiujn li volas apliki tiun tagon. </p>

<p><b>Lingvoj:</b> La magiisto povas interŝanĝi la drakonan kontraŭ unu el siaj kromaj lingvoj de sia popolo. </p>

<p id="arkana-skolo"><b>Arkana skolo:</b> La magiisto elektas sorĉskolon por specialiĝo. Tiel li ricevas kromajn sorĉojn kaj eksterordinarajn eblecojn kiuj baziĝas sur tiu skolo. La elekto devas esti farita je la 1a nivelo kaj poste ne plu povas esti ŝanĝataj. Magiistoj kiuj ne specialiĝas, ricevas la ĝeneralan skolon de la magio. </p>
<p> Magiisto kiu specialiĝas en unu skolo, devas elekti du skolojn kiujn li ne scipovas uzi. Tio reprezentas la mankon de scio en unu fako por atingi la majstrecon en alia. Magiisto kiu preparas sorĉon de malpermesata skolo devas uzi du sorĉunuojn de ties grado por memori la sorĉon. Ekzemple devas magiisto kiu elektis elvokadon kiel malpermesata skolo, uzi du el siaj disponeblaj sorĉunuoj de la tria grado por memori fajroglobon por tiu tago. Krome specialisto ricevas malbonon -4 por ĉiuj kapablecĵetoj por krei magiajn objektojn kiuj havas sorĉon de malpermesata skolo kiel kondiĉo. Universala magiisto povas prepari sorĉojn de ĉiuj skoloj sen restriktoj. </p>
<p> Ĉiu arkana skolo donas al magiisto kelkajn skolajn eblecojn. Plie specialistaj magiistoj ricevas unu plian sorĉunuon por ĉiu sorĉgrado ekde la 1a kiujn ili povas efektivigi. Tiun sorĉunuon la magiisto povas plenumi per sorĉo de sia speciala skolo. Tiu sorĉo devas esti en la sorĉolibro de la magiisto. La magiisto ankaŭ povas prepari sorĉon kiu estas modifita per metamagia talento, sed tio uzas sorĉunuon de pli granda grado. Universalaj magiistoj ne ricevas aldonan sorĉunuon. </p>

<p id="arkana-ligo"><b>Arkana ligo (EE aŭ SSE):</b> Je la 1a nivelo la magiisto faras potencan ligon kun objekto aŭ besto. Tiu ligo povas esti laŭ du formoj. La unua estas intimulo kiu sekvas la kutimajn regulojn (vidu <a href="#intimulo">intimulo</a>). La dua estas la fokuso, ligo kun objekto kiu subtenas la magiiston kun siaj sorĉoj kaj povas esti transsorĉata por plifortigi la povon de la fokuso. </p>
<p>Ekde tiam kiam li decidis por unu formon de ligo, li ne plu povas ŝanĝi tion. La reguloj por la intimulo troveblas malsupre kaj la reguloj por la fokuso en la venontaj paragrafoj. </p>
<p> Magiisto kiu elektas la fokuson kiel ligon, jam havas ĝin je la ludkomenco. Por taŭgi kiel fokuso la objekto devas esti de la postaj kategorioj: amuleto, ringo, bastoneto, bastono aŭ armilo. Tiuj objektoj ĉiam estas majstraj verkoj. Armiloj kiuj estas akiritaj je la 1a nivelo ne rajtas esti faritaj el specialaj materialoj. Se la fokuso estas amuleto aŭ ringo, li devas esti portata kaj okupas la rilatan ekipaĵolokon. Ĉiuj aliaj objektoj devas esti tenataj en la manoj de la magiisto por efektivigi siajn povojn. Se la magiisto provas efektivigi sorĉon sen sia fokuso, li devas sukcese fari ĵeton de sorĉkono. La MFG estas 20 + sorĉogrado. </p>
<p> La fokuso povas esti uzata unufoje ĉiutage por efektivigi sorĉon kiu troveblas en la sorĉolibro de la magiisto. Li povas efektivigi ĝin eĉ se la sorĉo ne estas preparita aŭ jam estas uzita. La sorĉo efektiviĝas same kiel preparita sorĉo. Li ne povas esti modifita per metamagiaj talentoj aŭ aliaj eblecoj. La fokuso ne povas esti uzata por efektivigi sorĉojn de malpermesata skolo. (Vidu <a href="#arkana-skolo">arkana skolo</a>).
<p> La magiisto povas transsorĉi sian fokuson tiel kiel li havus la necesajn talentojn sed nur se li plenumas la kondiĉon por la necesa nivelo de la talento. Ekzemple devas magiisto, kiu havas sian ponardon kiel fokuso, havi la 5an nivelon por transsorĉi sian ponardon kun magiaj ecoj (vidu la talenton produkti magiajn armilojn kaj armaĵojn). Se temas pri bastono, ĝi perdas sian transsorĉon kiam la lasta ŝargo estis uzata. Ĝi tamen tenas ĉiujn eblecojn kiel fokuso kaj povas esti uzata por produkti novan bastonon. La eblecoj de la fokuso inkluzive ĝia transsorĉo nur povas esti uzataj de ĝia magiisto kiu havas la rilatan ligon. Se la posedanto mortas aŭ la objekto estas anstataŭigata, la fokuso perdas ĉiujn siajn eblecojn kaj iĝas kutima objekto de majstra kvalito. </p>
<p> Se la fokuso estas damaĝita, liaj trafopoentoj iĝas plenaj kiam la magiisto venontfoje preparas siajn sorĉojn. Se la fokuso estas detruita aŭ perdita, li povas esti anstataŭigata post unu semajno. Por tio necesas ritualo kiu kostas 200 mo por ĉiu magiistonivelo kaj daŭras ok horojn. Tiuj anstataŭaj objektoj tamen ne havas la transsorĉojn de la antaŭa objekto. La magiisto povas determini magian objekton kiel fokuso. Tio funkcias same kiel la anstataŭigo de detruita aŭ perdita fokuso. La nova fokuso tamen gardas la antaŭajn magiajn ecojn apud siaj novaj avantaĝoj kaj malavantaĝoj kiel fokuso. </p>

<p id="sorcxartifikoj"><b>Sorĉartifikoj:</b> Magiisto povas ĉiutage prepari kvanton da sorĉartifikoj aŭ sorĉoj de la nula grado kiel indikita en la supra tabelo sub sorĉoj por unu tago. </p>

<p id="krei-skribajxorulon"><b>Krei skribaĵorulon:</b> Je la 1a nivelo la magiisto ricevas la talenton krei skribaĵorulon kiel kroma talento. </p>

<p id="kromaj-talentoj"><b>Kromaj talentoj:</b> Je la 5a, 10, 15a kaj 20a nivelo la magiisto ricevas kroman talenton. Devas esti metamagia talento, talento por krei magiajn objektojn aŭ sorĉomajstreco. La magiisto devas tamen plenumi ĉiujn kondiĉojn de la talento inkluzive la minimuma sorĉnivelo. La magiisto ricevas tiujn talentojn aldone al tiuj kiujn li ricevas dum altiĝo je nivelo. Ĉe la talentoj de la nivelaltiĝo li ne estas devigita elekti metamagian talenton, sorĉomajstrecon aŭ talenton por krei magiajn objektojn. </p>

<p id="sorcxolibroj"><b>Sorĉolibroj:</b> La magiisto devas ĉiutage studi sian sorĉolibron por prepari siajn sorĉojn. Li ne povas prepari sorĉojn kiuj ne estas en lia sorĉolibro krom legi magion kiun li ĉiam povas prepari. </p>
<p> La magiisto komencas la ludon kun sorĉolibro kiu enhavas ĉiujn sorĉartifikojn (krom tiuj de malpermesataj skoloj). Li ricevas tri sorĉojn de la unua grado kaj aldone unu plia de la unua grado por ĉiu poento de sia inteligentecobono. Je la altiĝo al nova nivelo li ricevas du novajn sorĉojn. Tiuj povas esti de ĉiu grado kiun li povas uzi (rilate al la nova nivelo). La magiisto povas ĉiam kopii sorĉojn de aliaj magiistoj al sia sorĉolibro. </p>

<h2>Intimulo</h2>
<p>Intimulo estas besto kiu estas elektita de magiisto por helpi lin ĉe liaj studadoj. Ĝi gardas sian aspekton, siaj vivpoentoj, sian bazatakan bonon, siajn bazajn bonojn por savĵetoj, kapablecojn kaj talentojn de besto kiun ĝi estis. Tamen la intimulo estas traktita kiel magia besto por ĉiuj efikoj kiuj dependas de la bestospeco. Nur normala, neŝanĝita besto povas iĝi intimulo. Bestkunulo ne povas samtempe esti intimulo. </p>
<p> La majstro de la intimulo ricevas specifajn eblecojn de la intimulo kiel estas indikita en malsupra tabelo. </p>
<p> La niveloj de diversaj klasoj kiuj ricevas intimulon sumiĝas por determini la eblecojn de la intimulo kiuj dependas de la nivelo de la majstro. </p>
<p> Se intimulo perdiĝis aŭ mortis, ĝi povas esti anstataŭigota post unu semajno per speciala ritualo kiu kostas 200 mo por ĉiu magiistonivelo. La ritualo daŭras ok horojn. 

