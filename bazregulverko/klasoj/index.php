<h3>Altiĝo de rolulo</h3>

<p> Roluloj altiĝas en niveloj kiam ili kolektis sufiĉe da spertpoentoj - kutime tio okazas dum la fino de ludseanco kiam la ludgvidanto disdonas spertpoentojn. Kiam la rolulo altiĝas dum la ludo, li fariĝas vere potenca en la ludo. Li povas gvidi naciojn aŭ superfortigi ilin. </p>

<p> La nivelaltiĝo funkcias simile kiel la kreado de la rolulo, nur la atribuoj, la popolo kaj pasintaj elektebloj rilate al klasoj, kapablecoj kaj talentoj ne povas esti ŝanĝitaj. La altiĝo de nivelo kutime donas al la rolulo novajn eblecojn, pliajn kapablecrangojn, pliajn vivpoentojn kaj eble altiĝon de atributvaloro aŭ talenton (vidu tabelon: rolulsperto kaj niveldependaj avantaĝoj). </p>

<p>Por la altiĝo en klasnivelo aŭ en nova nivelo en nova klaso (vidu klaskombinaĵojn malsupre), oni devus atenti la postan sinsekvon. Unue vi elektas por la nova nivelo la klason por la altiĝo. Vi devas plenumi ĉiujn kondiĉojn por tiu nivelo antaŭ fari la postajn paŝojn. Due oni faras eblan altiĝon de atributvaloro. Trie ĉiuj klaseblecoj por tiu nivelo estas skribataj en la rolulformularo kaj la novaj vivpoentoj estas ĵetataj. Poste novaj kapablecoj kaj talentoj estas elektataj. Por pliaj informoj kiam atributoj altiĝas kaj talentoj aldoniĝas vidu la tabelon: rolulsperto kaj niveldependaj avantaĝoj. </p>

<h3>Klaskombinaĵoj</h3>
<p>Anstataŭ akiri eblecojn de la venonta nivelo en la aktuala klaso de la rolulo, oni povas decidi akiri la eblecojn de la unua nivelo de nova klaso kaj aldoni ilin al siaj nunaj eblecoj. Tio nomiĝas klaskombinaĵo por la rolulo. </p>
<p>Ni supozu ekzemple ke batalisto de la kvina nivelo decidas akiri arkanajn spertojn kaj je la altiĝo al la sesa nivelo elektas unu nivelon kiel magiisto. Tiu rolulo havas la eblecojn kaj karakterizaĵojn de batalisto de la kvina nivelo kaj de magiisto de la unua nivelo, tamen la rolulo estas de la sesa nivelo (liaj klasniveloj estas 5 kaj 1, sed lia tuta nivelo estas 6). Li daŭre gardas ĉiujn kromajn talentojn kiujn batalisto de la kvina nivelo havas kaj nun ankaŭ povas efektivigi sorĉojn de la unua grado kaj elekti arkanan skolon. Li aldonas ĉiujn siajn vivpoentojn, bazatakajn bonojn kaj bonojn por savĵetoj de magiisto de la unua nivelo al la valoroj kiujn li akiris de la kvin niveloj de batalisto.</p>
<p>Atentu ke estas multaj efikoj kaj kondiĉoj kiuj dependas de la nivelo de la rolulo aŭ de liaj vivkubetoj. Tiuj efikoj ĉiam rilatas al la tuta nivelo aŭ liaj vivkubetoj kiujn havas la rolulo. Esceptoj estas la karakterizaĵoj de la klaso kiuj plejparte dependas de la klasniveloj de la rolulo. </p>

<h3>Preferata klaso</h3>
<p>Ĉiu rolulo komencas la ludon kun preferata klaso laŭ sia elekto. Kutime tio estas klaso de la unua nivelo. Ĉiam kiam la rolulo altiĝas en sia preferata klaso li ricevas aŭ +1 vivpoenton aŭ +1 kapablecorangon. La elekto de preferata klaso ne plu povas esti ŝanĝita post la kreado de la rolulo. Same la elekto de aldona vivpoento aŭ kapablecorango dum altiĝo (ankaŭ de la 1a nivelo) ne povas esit ŝanĝita poste. Prestiĝklasoj neniam povas esti preferataj klasoj (vidu <a href="../prestigxklasoj/">Prestiĝklasoj</a>).</p>

