<p><b>Aĉeto:</b> Ĉiu rolulo ricevas kvanton de tiel nomataj plialtigaj poentoj. Tiuj povas esti uzataj por altigi siajn atributvalorojn. Ĉe tiu ĉi metodo ĉiuj atributoj havas komencan valoron 10. Ĉiu atributvaloro povas esti altigata per elspezo de altigaj poentoj. Same vi povas akiri altigajn poentojn se vi malaltigas viajn komencajn atributvalorojn. Per tiu metodo neniu valoro rajtas esti malaltigata sub 7 kaj altigata super 18. Vidu la tabelon "kostoj de altigaj poentoj por atributŝanĝoj". Post ĉiuj altigaj poentoj estas elspezitaj, la popolbonoj estas aplikataj. <p>
<p>La kvanto da altigaj poentoj kiujn vi ricevas dependas de la tipo de aventuro kiun vi ludas. Kutime estas 15 altigaj poentoj kiujn vi povas elspezi por la atributvaloroj. Mezkvantaj neludantroluloj (NLR) kutime nur ricevas 3 poentojn. La tabelo altigaj poentoj proponas ekzemplajn altigajn poentojn por kelkaj stiloj de aventuroj. La aĉetometodo subtenas decidojn de ludantoj kaj certigas ekvilibrajn rolulojn. Tiu sistemo kutime estas uzata por organizataj ludokazaĵoj. </p>
<p><b>Provizo de kubetoj:</b> Ĉiu rolulo ricevas provizon de 24k6 por determini siajn atributvalorojn. Antaŭ vi ĵetas, decidu kiom da kubetoj vi volas uzi por kiu atributo. Devas esti minimume 3k6 por ĉiu atributo. Kiam vi decidis kiom da kubetoj vi volas uzi por kiu atributo, vi ĵetas la kubetojn kaj sumigas la tri plej altajn kubetojn. Por krei pli potencajn rolulojn la provizo povas esti pligrandigita al 28k6. Roluloj kiuj estas kreitaj tiamaniere estas simile potenca ol tiuj de la normala metodo. </p>

<h3>Determini bonojn</h3>
<p>Post la aplikado de popolmodifiloj ĉiu atributo havas modifilon de -5 al +5. La tabelo "atributmodifiloj kaj kromaj sorĉoj por unu tago" listigas la modifilon depende de la atributvaloro. Ĝi ankaŭ listigas eblajn kromajn sorĉojn kio povas esti interesa por vi se vi rolas sorĉkonanton. La atributmodifilo estas la nombro kiun vi aldonas al kubetoĵetoj kiam via rolulo faras ion kio rilatas al tiu atributo. Pozitivaj modifiloj nomiĝas bonoj kaj negativaj nomiĝas malbonoj. </p>

<h3 id="atributoj-kaj-sorcxkonantoj">Atributoj kaj sorĉkonantoj</h3>
<p> La atributo kiu gravas por kromaj sorĉoj dependas de la sorĉkonanto: inteligenteco por magiistoj, saĝeco por klerikoj, druidoj kaj arbaranoj kaj karismo por sorĉistoj, paladinoj kaj bardoj. Krom alta atributvaloro la sorĉkonanto devas havi sufiĉe grandan klasnivelon por efektivigi sorĉojn de la respektiva sorĉgrado. Vidu la klaspriskribon por pliaj detaloj. </p>

