<p><b>Sorĉoj:</b> La bardo povas efektivigi arkanajn sorĉojn kiujn li elektas de la sorĉlisto por bardoj. Li povas efektivigi ĉiujn sorĉojn kiujn li konas sen prepari ilin. Ĉiu barda sorĉo havas parolan komponanton (kanto, poezio, muziko). Por lerni kaj apliki sorĉon la bardo devas havi minimume karismon de 10 + sorĉgrado de la sorĉo. La malfacilecogrado de savĵetoj kontraŭ sorĉoj de la bardo estas 10 + sorĉgrado de la sorĉo + KAR-modifilo de la bardo. </p>
<p>Kiel aliaj sorĉkonantoj la bardo nur povas efektivigi limigitan kvanton da sorĉoj dum unu tago depende de sia nivelo. Lia dumtaga kvanto de sorĉoj estas indikita en la supra tabelo. Aldone li ricevas kromajn sorĉojn por altaj karismovaloroj (vido paragrafon: <a href="../ekludi/#atributoj-kaj-sorcxkonantoj">Atributoj kaj sorĉkonantoj</a>). </p>
<p>La elekto por sorĉoj de la bardo estas tre limigita. Je la komenco la bardo konas kvar sorĉojn de la 0a grado kaj du sorĉojn de la 1a grado. Por ĉiu nivelo li ricevas unu aŭ pli novajn sorĉojn kiel indikita en la apuda tabelo. (Alie ol la taga kvanto da sorĉoj la kvanto da konataj sorĉoj ne ŝanĝiĝas laŭ la karismo de la bardo.) </p>
<p>Je la 5a nivelo kaj poste je ĉiu plia 3a nivelo (8a, 11a, ktp.) la bardo povas interŝanĝi sorĉon kiun li lernis kontraŭ alian novan. Li tiam perdas la malnovan kaj anstataŭigas ĝin per la nova. Ambaŭ sorĉoj devas esti de la sama grado kaj la grado devas esti minimume unu grado sub la plej alta grado de sorĉo kiun konas la bardo. La bardo nur rajtas ŝanĝi unu sorĉon kaj devas fari la elekton kiam li ricevas novajn sorĉojn. </p>
<p>Bardoj ne devas prepari siajn sorĉojn, sed li povas efektivigi ĉiun sorĉon kiun li konas se li ankoraŭ havas liberajn sorĉojn por la tago. </p>

<p id="bardoscio"><b>Bardoscio:</b> La bardo aldonas sian duonan bardonivelon (minimume 1) al ĉiuj scioĵetoj kaj li povas uzi ĉiun sciokapablecon sen lerni ĝin. </p>

<p id="bardoprezentado"><b>Bardoprezentado:</b> La bardo havas sperton uzi sian kapablecon roli por efektivigi magian efikon al tiuj en sia ĉirkaŭaĵo, ankaŭ al si mem se li volas. Li pobas uzi tiun eblecon dum tiom da rondoj por unu tago kiom egalas al 4 + lia KAR-modifilo. Je ĉiu nivelaltiĝo aldoniĝas du pliaj rondoj por unu tago. Ĉiuronde la bardo povas prezenti ajnan prezentadon kiun li konas depende de lia nivelo. </p>
<p>Komenci bardoprezentadon estas bazago, sed daŭrigi ĝin en venontaj rondoj estas libera ago. Por ŝanĝi la bardoprezentadon de unu prezentado al alia la bardo devas fini la nunan prezentado por komenci la alian per bazago. La bardoprezentado ne povas esti pertubita, tamen ĝi finiĝas subite se la bardo estas mortigita, paralizita, svenanta, senkonscia aŭ io alia malpermesas lin uzi liberan agon por daŭrigi la bardoprezentadon. La bardo ne povas fari pli ol unu prezentadon samtempe. </p>
<p>Ekde la 7a nivelo la bardo povas komenci bardoprezentadon per movago anstataŭ per bazago. Ekde la 13a nivelo li povas komenci bardoprezentadon per rapida ago. </p>
<p>Ĉiu bardoprezentado havas videblaj komponantoj, aŭdeblaj komponantoj aŭ ambaŭ. </p>
<p>Se la bardoprezentado havas aŭdeblan komponanton la celoj devas kapabli aŭdi la bardon por ke la prezenado havas sian efikon. Multaj prezentadoj ankaŭ dependas de la lingvo. Surda bardo havas fuŝŝancon de 20% se li provas prezentadon kiu dependas de la aŭdpovo. Se li fuŝas prezentadon, ĝi tamen elkalkuliĝas de la taga kvanto de rondoj. Surdaj kreaĵoj estas imunaj kontraŭ bardoprezentadoj kun aŭdeblaj komponantoj. </p>
<p>Se la bardoprezentado havas videblajn komponanton la celoj devas kapabli vidi la bardon por ke la prezenado havas sian efikon. Blinda bardo havas fuŝŝancon de 50% se li provas prezentadon kiu dependas de la vidpovo. Se li fuŝas prezentadon, ĝi tamen elkalkuliĝas de la taga kvanto de rondoj. Blindaj kreaĵoj estas imunaj kontraŭ bardoprezentadoj kun videblaj komponantoj. </p>
<ul>
    <li id="kontrauxkanto"><em>Kontraŭkanto (SNE):</em> Je la 1a nivelo la bardo lernas kontraŭkanton kontraŭ magiaj efikoj surbaze de sono (nur paraloaj sorĉkomponantoj ne sufiĉas por tio). Dum la kontraŭkanto la bardo faras ĉiuronde kapablecoĵeton por roli (blov-, klavar-, kord-, perkutinstrumento aŭ kanto). Ĉiu kreaĵo ene de 9 m inkluzive la bardo mem kiuj estas influataj de magia efiko surbaze de sono povas uzi la rezulton de tiu ĵeto anstataŭ sia propra savĵeto se tio estas pli bona. Se kreaĵo en la influsfero jam estas viktimo de sona aŭ lingva nesubita efiko, ĝi ricevas ĉiuronde aldonan savĵeton kiam ĝi aŭdas la kontraŭkanton. Por tio la kreaĵo devas uzi la rezulton de la rolkapablecĵeto anstataŭ propra savĵeton. La kontraŭkanto nur funkcias ĉe efikoj kiuj permesas savĵetojn. La kontraŭkanto postulas aŭdeblan komponanton.</li>
    <li id="distrajxo"><em>Distraĵo (SNE):</em> Ekde la 1a nivelo la bardo povas uzi tiun prezentadon kontraŭ magiaj efikoj surbaze de vido. Ĉiuronde li povas fari kapablecoĵeton por roli (danci, komedio, retoriko aŭ teatro). Ĉiu kreaĵo ene de 9 m inkluzive la bardo mem kiuj estas influataj de iluzio (fantaziaĵo aŭ halucino) povas uzi la rezulton de la rolĵeto de la bardo anstataŭ sia propra savĵeto se tio estas pli alta. Se kreaĵo en la influsfero jam estas viktimo de nesubita iluzio (fantaziaĵo aŭ halucino), ĝi ricevas ĉiuronde aldonan savĵeton kiam ĝi vidas la distraĵon. Por tio la kreaĵo devas uzi la rezulton de la rolkapablecĵeto anstataŭ propra savĵeton. La distraĵo nur funkcias se la efiko permesas savĵetojn. La distraĵo postulas videblan komponanton.</li>
    <li id="fascini"><em>Fascini (SNE):</em> La bardo povas uzi tiun eblecon ekde la 1a nivelo por fascini unu aŭ pliajn kreaĵojn per muziko aŭ poezio. Por ke kreaĵo povas esti fascinata, ĝi devas esti ene de 27 m, ĝi devas aŭdi kaj vidi la bardon kaj devas kapabli atenti la bardon. Ankaŭ la bardo devas vidi sian celon. Distraĵoj kiel proksima batalo malhelpas la efikon de la prezentado. Por ĉiuj tri niveloj post la 1a nivelo la bardo povas fascini unu plian kreaĵon. <br>
    Ĉiu kreaĵo en la influsfero devas sukcesi voloĵeton (MFG 10 + &frac12; bardonivelo + KAR-modifilo de la bardo) por kontraŭstari la efikon. Se la voloĵeto de la kreaĵo sukcesas, la bardo ne plu povas provi fascini tiun kreaĵon dum la venontaj 24 horoj. Se la voloĵeto malsukcesas, la celo sidas trankvile kaj spektas la prezentadon sen fari aliajn agojn tiel longe kiel la bardo faras sian prezentadon. Kiam la kreaĵo estas fascinita, ĝi ricevas malbonon -4 por kapablecĵetoj por reagoj kiel ekzemple percepto. Ĉiu ebla danĝero permesas novan volĵeton al la kreaĵo. Ĉiu evidenta danĝero kiel elingigi armilon, efektivigi sorĉon aŭ direkti armilon al la celo, finas la efikon aŭtomate. <br>
    Fascini estas transsorĉa (deviga) kaj mensinflua ebleco. Fascini postulas videblajn kaj aŭdeblajn komponantojn por havi efikon.</li>
    <li id="inspiri-kuragxon"><em>Inspiri kuraĝon (SNE):</em> Per tiu prezentado la bardo povas ekde la 1a nivelo kolekti la kuraĝon de siaj kamaradoj (inkluzive si mem), firmigi ilin kontraŭ angoro kaj fortigi ilian batalforton. La aliancanoj devas aŭdi la bardon por profiti de la ebleco. Aliancanoj ricevas moralbonon +1 por savĵetoj kontraŭ ravi kaj timigi. Aldone ili ricevas kompetentobonon +1 por atakĵetoj kaj armilo-damaĝoĵetoj. Je la 5a nivelo kaj je ĉiu sesa plia nivelo la bono altiĝas je +1 ĝis maksimumo de +4 je la 17a nivelo. La inspiri kuraĝon estas mensinflua ebleco. Ĝi postulas videblajn aŭ aŭdeblajn komponantojn. La bardo devas elekti je la komenco de sia prezentado kiun komponanton li uzas. </li>
    <li id="inspiri-sukceson"><em>Inspiri sukceson (SNE):</em> Bardo de 3a aŭ pli alta nivelo povas uzi tiun eblecon por helpi kamaradon plenumi taskon. La kamarado devas esti ene de 9 m de la bardo kaj devas aŭdi lin. La kamarado ricevas kompetentobonon +2 por ĵetoj de certa kapableco tiel longe kiel li aŭdas la prezentadon de la bardo. Tiu bono altiĝas je +1 je ĉiu kvara nivelo post la 3a nivelo de la bardo (+3 je la 7a nivelo, +4 je la 11a, +5 je la 15a kaj +6 je la 19a nivelo). Kelkaj taskoj ne taŭgas por helpo de tiu ebleco kiel ekzemple ĵeto por sekreteco. La ludgvidanto povas malpermesi la uzon de tiu ebleco en tiaj situacioj. La bardo ne povas esti mem celo de tiu ebleco. La inspiri sukceson postulas aŭdeblajn komponantojn. </li>
    <li id="suflori"><em>Suflori (SSE):</em> Bardo de la 6a aŭ pli alta nivelo povas provi suflori (kiel samnoma sorĉo) al celo kiu jam estas fascinita (vidu supren). Tiu ebleco ne interrompas la efikojn de fascini sed necesas bazago por aktivi ĝin (aldone al libera ago por daŭrigi fascini). La bardo povas pli ol unu fojon uzi tiun eblecon al unu kreaĵo dum unu prezentado. <br>
    Suflori ne kalkuliĝas por la taga kvanto da bardoprezentadoj. Voloĵeto (MFG 10 + &frac12; bardonivelo + KAR-modifilo de la bardo) nuligas la efikon. Tiu ebleco nur influas unu celon. Suflori estas transsorĉa (deviga) kaj mensinflua kapableco kiu estas lingvodependa kaj postulas aŭdeblajn komponantojn. </li>
    <li id="funebra-kanto"><em>Funebra kanto (SNE):</em> Bardo de 8a aŭ pli alta nivelo povas uzi tiun eblecon por semi angoron en la korojn de malamikoj kio igas ilin emociitaj. Por tio la celo devas esti ene de 9 m de la bardo kaj devas aŭdi kaj vidi lin. La efiki daŭras tiom longe kiel la bardo estas en la influsfero kaj daŭrigas la prezentadon. Tiu prezentado ne povas igi celon timplena aŭ panika ankaŭ se la celo jam estas emociita per alia efiko. La funebra kanto estas mensinflua timefiko kaj postulas aŭdeblajn kaj videblajn komponantojn. </li>
    <li id="kanto-de-grando"><em>Kanto de grando (SNE):</em> Bardo de 9a aŭ pli alta nivelo povas uzi tiun prezentadon por generi veran grandon en si mem aŭ en kamaradon ene de 9 m kaj fortigi la batalkapablecojn. Por ĉiu tria nivelo ekde la 9a nivelo la bardo povas atingi unu plian kamaradon per tiu kanto (du ĉe la 12a nivelo, tri je la 15a kaj kvar je la 18a). La bardo devas esti aŭdebla kaj videbla al la kamarado. Kreaĵo kiu estas la celo de la kanto de grando ricevas du kromajn vivkubetojn (2k10), la rilatan kvanton da vivpoentoj (la vivpoentoj de la vivkubetoj estas modifitaj de la KON-modifilo), kompetentobono +2 por atakoj kaj kompetentobono +1 por persistecoĵetoj. La vivkubetoj ankaŭ kalkuliĝas por efikoj kiuj dependas de la vivkubetoj. La kanto de grando estas mensinflua ebleco kaj postulas aŭdeblajn kaj videblajn komponantojn. </li>
    <li id="refresxiganta-prezentado">Refreŝiganta prezentado (SNE):</em> Bardo de 12a aŭ pli alta nivelo povas uzi sian prezentadon por elvoki efikon kiu funkcias kiel amase-sanigi-gravajn-vundojn (bardonivelo estas la sorĉnivelo). Aldone la prezentado forprenas la efikojn elĉerpita, malsaneta kaj emociita. Por efektivigi la efikojn de tiu prezentado la bardo devas daŭrigi la prezentadon seninterrompe dum kvar rondoj. La bardo devas esti videbla kaj aŭdebla al la celoj dum la prezentado. La refreŝiganta prezentado trafas ĉiujn celojn kiuj restis dum la prezentado ene de 9 m. La refreŝiganta prezentado postulas videblajn kaj aŭdeblajn komponantojn. </li>
    <li id="angoriga-melodio"><em>Angoriga melodio (SSE):</em> Bardo de 14a aŭ pli alta nivelo povas angorigi malamikojn. Malamikoj kiuj povas aŭdi la bardon kaj estas ene de 9 m de la bardo, estas influataj de la efiko kaj povas fari voloĵeton (MFG 10 + &frac12; bardonivelo + KAR-modifilo de la bardo) por nuligi la efikon. Se la ĵeto estas sukcesa, la malamiko estas imuna dum 24 horoj kontraŭ tiu ebleco. Se li malsukcesas la ĵeton, li estas timplena kaj fuĝas tiel longe kiel li povas aŭdi la prezentadon. La angoriga melodio postulas aŭdeblajn komponantojn. </li>
    <li id="inspiri-heroecon"><em>Inspiri heroecon (SNE):</em> Bardo de la 15a aŭ pli alta nivelo povas elvoki ĉe si mem aŭ ĉe unu kamarado ene de 9 m apartan herokuraĝon. Por ĉiu plia tria nivelo post la 15a nivelo la bardo povas inspiri unu plian kamaradon. Por inspiri heroecon la bardo devas esti videbla kaj aŭdebla al ĉiuj rilataj kamaradoj. Kreaĵoj kiuj estas inspiritaj de heroeco  ricevas moralbonon +4 por savĵetoj kaj evitbonon +4 por la armaĵklaso. Tiu efiko daŭras tiom longe kiel la celoj povas sekvi la prezentadon. Inspiri heroecon estas mensinflua efiko kiu postulas aŭdeblajn kaj videblajn komponantojn. </li>
    <li id="amassuflori"><em>Amassuflori (SSE):</em> Bardo de la 18a aŭ pli alta nivelo povas uzi tiun eblecon kiu funkcias same kiel suflori, nur kun la diferenco ke li povas suflori al ajna kvanto da kreaĵoj kiuj jam estas fascinitaj. Amassuflori estas transsorĉa (deviga) kaj mensinflua ebleco kiu estas lingvodependa kaj postulas aŭdeblajn komponantojn. </li>
    <li id="mortiga-melodio"><em>Mortiga melodio (SNE):</em> Je la 20a nivelo la bardo povas uzi tiun prezentadon por igi malamikojn morti de ĝojo aŭ ĉagreno. La bardo devas esti videbla kaj aŭdebla al la celo dum la prezentado kiu daŭras unu plena rondo kaj la celo devas esti ene de 9m. La viktimo povas nuligi la efikon per voloĵeto (MFG 10 + &frac12; bardonivelo + KAR-modifilo de la bardo). Se la voloĵeto sukcesas la celo estas ŝanceliĝanta dum 1k4 rondoj kaj ne plu povas esti la viktimo de tiu ebleco dum la venontaj 24 horoj. Se la savĵeto malsukcesas, la celo mortas. La mortiga melodio estas mensinflua mortefiko kiu postulas aŭdeblajn kaj videblajn komponantojn. </li>
</ul>

<p id="sorcxartifikoj"><b>Sorĉartifikoj:</b> La bardo lernas kvanton da sorĉartifikoj aŭ sorĉoj de la grado nul kiel indikita en la tabelo: konataj sorĉoj de la bardo. Tiuj sorĉoj efektiviĝas kiel aliaj sorĉoj. Tamen la bardo povas efektivigi ilin tiom ofte kiel li volas. </p>

<p id="multflanka-prezentado"><b>Multflanka prezentado (EE):</b> Je la 2a nivelo la bardo povas elekti unu tipon de la kapableco roli. Li povas uzi sian bonon en roli anstataŭ la bonoj en la rilataj kapablecoj (vidu malsupren). Se li anstataŭigas kapablecon tiamaniere, li uzas sian tutan kapablecbonon de roli inkluzive la bono por klaskapableco. Ne gravas ĉu la anstataŭigita kapableco ne estis klaskapableco aŭ kiom rangojn la kapableco havis. Je la 6a nivelo kaj je ĉiu plia kvara nivelo la bardo povas elekti plian tipon de roli por anstataŭigi kapablecojn. </p>
<p><b>La tipoj de roli kaj iliaj rilataj kapablecoj estas:</b> blovinstrumentoj (diplomatio, interrilati kun bestoj), danci (akrobataĵo, flugi), kanto (blufi, malkovri motivon), klavaraj instrumentoj (diplomation, timigi), komedio (blufi, timigi), kordinstrumentoj (blufi, diplomatio), perkutinstrumentoj (interrilati kun bestoj, timigi), retoriko (diplomatio, malkovri montivon), teatro (alivesti, blufi). </p>

<p id="scianta"><b>Scianta (EE):</b> Je la 2a nivelo la bardo iĝas rezistenta kontraŭ bardoprezentadoj de aliaj kaj kontraŭ sonefikoj ĝenerale. Li ricevas bonon +4 por savĵetoj kontraŭ bardoprezentadoj, sonatakoj kaj ligvo-rilataj efikoj. </p>

<p id="erudiciulo"><b>Erudiciulo (EE):</b> Je la 5a nivelo la bardo fariĝas vera erudiciulo. Li povas preni 10  por ĵetoj de sciokapablecoj en kiuj li havas rangojn, se li volas. Unufoje tage li povas preni 20 kiel bazago por savĵeto. Por ĉiu plia sesa nivelo post la 5a nivelo li povas uzi tion unu plian fojon ĝis maksimumo de tri fojoj ĉe la 17a nivelo. </p>

<p id="multtalentulo"><b>Multtalentulo (EE):</b> Je la 10a nivelo la bardo rajtas uzi ĉiun kapablecon eĉ se li kutime nur rajtus uzi ĝin lernite. Je la 16a nivelo ĉiuj kapablecoj kalkuliĝas kiel klaskapablecoj. Je la 19a nivelo li povas preni 10 por ĉiu kapableco eĉ se tio ne estus permesita. </p>

