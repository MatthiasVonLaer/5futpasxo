<p>Je la 4a nivelo kaj je ĉiu plia kvara nivelo (je la 8a, 12a, ktp.) la batalisto povas elekti novan kroman talenton kiun li ricevas anstataŭ iu ajna jam lernita kroma talento. Efektive li ŝanĝas malnovan kroman talenton kontraŭ nova kroma talento. La malnova kroma talento ne rajtas esti uzita kiel kondiĉo por alia talento, por prestiĝklaso aŭ alia ebleco. La batalisto nur povas ŝanĝi talenton kiam li atingas la antaŭe indikitajn nivelojn. Tiam kiam li ricevas la kroman talenton, li devas decidi ĉu li faras ŝanĝon aŭ ne. </p>

<p id="braveco"><b>Braveco (EE):</b> Ekde la 2a nivelo la batalisto ricevas bonon +1 por voloĵetoj kontraŭ timo. Tiu bono altiĝas je ĉiu kvara nivelo post la 2a je +1. </p>

<p id="armajxotrejnado"><b>Armaĵotrejnado (EE):</b> Ekde la 3a nivelo la batalisto lernas pli bone moviĝi en sia armaĵo. Kiam li portas armaĵon, la armaĵomalbono malplialtiĝas je 1 (ĝis minimumo de 0) kaj la maksimuma lertecobono, kiun permesas lia armaĵo, altiĝas je +1. Je ĉiu plia kvara nivelo (7a, 11a, 15a) la armaĵotrenado pliboniĝas je unu poento ĝis maksimumo de -4 por la armaĵomalbono kaj +4 por la maksimuma lertecobono. </p>
<p> Krome la batalisto povas moviĝi per normala moviĝrapideco en mezpeza armaĵo. Ekde la 7a nivelo la batalisto ankaŭ en peza armaĵo povas moviĝi per normala moviĝrapideco. </p>

<p id="armilotrejnado"><b>Armilotrejnado (EE):</b> Ekde la 5a nivelo la batalisto povas elekti unu el la grupoj de armiloj malsupre. Ĉiam kiam li faras atakon per armilo de la elektita grupo, li ricevas bonon +1 por atako kaj damaĝo. </p>
<p>Je ĉiu plia kvara nivelo (9a, 13a, 17a) la batalisto trejniĝas en nova grupo de armiloj. Li ricevas bonon +1 por atako kaj damaĝo por tiu grupo kaj la bono de la jam elektitaj grupoj altiĝas je +1. Ekzemple je la 9a nivelo la batalisto elektas novan grupon de armiloj kaj ricevas bonon +1 por tiuj armiloj. La bono por la grupo de armiloj kiun li elektis je la 5a nivelo altiĝas al +2. Bonoj de du parte kongruaj grupoj ne sumiĝas. La batalisto tiam uzas la pli bonan. La batalisto uzas tiun bonon ankaŭ por ĉiu batalomanovro kiun li faras per armilo de elektita grupo. La bono ankaŭ aldoniĝas al la batalmanovra defendo, se la batalisto defendas sin kontraŭ senarmigi aŭ frakasi armilon de tiu grupo. </p>
