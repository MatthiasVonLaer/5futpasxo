<h2>Klasaj karakterizaĵoj</h2>

<p> La sekvantaj eblecoj estas la klasaj karakterizaĵoj de la druido. </p>

<p><b>Sperto kun armiloj kaj armaĵoj:</b> La druido havas sperton kun la <a href="../ekipajxo/#ponardo">ponardo</a>, <a href="../ekipajxo/#serpo">serpo</a>, <a href="../ekipajxo/#klabo">klabo</a>, <a href="../ekipajxo/#lanco-kurta">lanco kurta</a>, <a href="../ekipajxo/#bastono-ferita">bastono ferita</a>, <a href="../ekipajxo/#lanco">lanco</a>, <a href="../ekipajxo/#sxtonjxetilo">ŝtonĵetilo</a>, <a href="../ekipajxo/#jxetsagoj">ĵetsagoj</a>, <a href="../ekipajxo/#cimitaro">cimitaro</a> kaj <a href="../ekipajxo/#falcxilo">falĉilo</a>. Aldone li havas sperton kun ĉiu natura armilo kiun li povas havi en zooformo (vidu <a href="#zooformo">zooformo</a>). Druidoj havas sperton kun malpezaj kaj mezpezaj armaĵoj sed ne povas porti metalan armaĵon. Ilia elekto do estas inter armaĵĵupo, ledaramaĵo aŭ felarmaĵo. La druido ankaŭ povas porti lignan kirason kiu per sorĉo estas transformita al ferligno kiu protektas lin kiel ŝtalo. Druidoj povas uzi ŝildojn krom turŝildoj sed nur povas uzi ŝîldojn kiuj estas fabrikitaj de ligno. </p>
<p> Druido kiu uzas malpermesatan armaĵon aŭ uzas malpermasatan ŝildon ne povas sorĉi nek efektivigi siajn supernaturajn kaj sorĉsimilajn klaskarakterizaĵojn dum 24 horojn. </p>

<p><b>Sorĉoj:</b> La druido efektivigas diajn sorĉojn kiujn li elektas de la sorĉlisto de druidoj, kiuj troveblas en ĉapitro <a href="../sorcxlistoj/">Sorĉlistoj</a>. Kelkaj el tiuj sorĉoj tamen povas kontraŭstari al la etiko, moralo aŭ pensmaniero de la druido kaj ne tiukaze ne estas aplikeblaj. Vidu <a href="#bonaj-malbonaj-lauxlegxaj-hxaosaj-sorcxoj">bonaj, malbonaj, laŭleĝaj kaj ĥaosaj sorĉoj</a>. La druido devas elekti kaj prepari siajn sorĉojn. </p>
<p>Por elekti kaj efektivigi sorĉon la druido devas havi minimume saĝecon de 10 + sorĉgrado. La malfacilecgrado por savĵetoj kontraŭ sorĉoj de la druido estas 10 + sorĉgrado + SAĜ-modifilo de la druido. </p>
<p>Kiel aliaj sorĉkonantoj la druido nur povas efektivigi certan kvanton da sorĉoj en unu tago. La sorĉoj por unu tago estas indikitaj en la supra tabelo. Aldone li ricevas kromajn sorĉojn laŭ la alteco de sia saĝeco (vidu paragrafon <a href="../ekludi/#atributoj-kaj-sorcxkonantoj">Atributoj kaj sorĉkonantoj</a>). </p>
<p>La druido pasigas unu horon ĉiutage en trancsimila meditado pri la misteroj de la naturo por reakiri siajn sorĉojn. Li povas elekti ĉiun sorĉon de la sorĉlisto por druidoj se li rajtas efektivigi la sorĉgradon. Li devas elekti dum la meditado kiujn sorĉojn li preparas por la tago. </p>

<p><b>Spontane sorĉi:</b> La druido povas uzi preparitan sorĉpovon por sorĉvenigi aliancanojn. La druido povas fordoni unu preparitan sorĉon por sorĉvenigi naturaliancanon de sama aŭ malpli alta grado. </p>

<p id="bonaj-malbonaj-lauxlegxaj-hxaosaj-sorcxoj"><b>Bonaj, malbonaj, laŭleĝaj kaj ĥaosaj sorĉoj:</b> La druido ne povas efektivigi sorĉojn kiuj kontraŭstaras lian pensmanieron aŭ la pensmanieron de lia dio (se li sekvas iun dion). Sorĉoj kiuj postulas certan pensmanieron havas rilatan indikon en sia priskribo. </p>

<p id="pregxoj"><b>Preĝoj:</b> La druido povas ĉiutage prepari certan kvanton da preĝoj aŭ sorĉoj de la 0a grado laŭ la supra tabelo en la kolumno "sorĉoj por unu tago". Tiuj sorĉoj efektiviĝas kiel aliaj sed ili ne eluziĝas kaj povas esti efektivigitaj denove. </p>

<p id="aldonaj-lingvoj"><b>Aldonaj lingvoj:</b> La druido povas aldone al la lingvoj de sia popolo leri la silvanan, la lingvon de arbaAKreaĵoj. </p>
<p> La druido lernas krome la sekretan lingvon druidan je la 1a nivelo. Tiun lingvon la druido ricevas aldone al siaj aliaj lingvoj. Al druidoj estas malpermesitaj instrui tiun lingon al aliaj. La druida havas propran alfabeton. </p>

<p id="alianco-kun-la-naturo"><b>Alianco kun la naturo:</b> Druido de la 1a nivelo faras aliancon kun la naturo. Tiu povas havi du formojn kaj la druido devas elekti unu. Aŭ la druido elektas intiman aliancon kun la naturo kiu permesas al li akiri unu el la klerikaj sferoj tero, fajro, aero, plantoj, bestoj, akvo aŭ vetero. Por determini la povojn kaj sorĉojn de la sfero la druidonivelo kalkuliĝas kiel klerikonivelo. Druido kiu elektas tiun formon de alianco ricevas unu kroman sorĉon por la sferosorĉo same kiel la kleriko. Li devas prepari unu sferosorĉon por tiu kroma sorĉo kaj ne povas uzi tiun sorĉon por spontane sorĉi. </p>
<p> Aŭ la druido druido elektas bestkunulon kiel elekto por sia alianco kun la naturo. La druido komencas la ludon kun bestkunulon kiu troveblas en la paragafo <a href="#bestkunuloj">bestkunuloj</a>. La kunulo estas fidela kamarado kaj akompanas la druidon konforme al sia specion. </p>
<p> Alie ol ĉe kutimaj bestoj la vivkubetoj, la atributoj, la kapablecoj kaj talentoj de la bestkunulo altiĝas en sama maniero kiel la druido altiĝas en nivelo. Se rolulo ricevas bestkunulon de diversaj fontoj, la efikaj druidoniveloj sumiĝas se temas pri determini la valorojn kaj eblecojn de la kunulo. Plejmulto de bestkunuloj krekas en grandeco se la druido atingas la 4an aŭ 7an nivelon depende de la specion de la kunulo. Se la druido separiĝas de sia bestkunulo, li povas post rito de seninterrompa preĝado dum 24 horoj en la ĉiAKaŭaĵo, kie la nova bestkunulo kutime havas sian vivospacon, trovi novan bestkunulon. Per tia rito la druido ankaŭ povas trovi novan kunulon se la ĝisnuna bestkunulo mortis. </p>

<p id="natursento"><b>Natursento (EE):</b> La druido ricevas kapablecbonon +2 por ĵetoj de scio (naturo) kaj transvivado. </p>

<p id="bestkunsento"> <b>Bestkunsento (EE):</b> La druido povas plibonigi la sintenon de besto kontraŭ li. Tiu ebleco funkcias kiel kapablecĵeto de diplomatio por ŝanĝi la sintenon de persono. La druido ĵetas 1k20 kaj aldonas sian druido-nivelon kaj sian KAR-modifilon. Dombestoj havas komence indiferentan sintenon, tamen sovaĝaj bestoj havas unue malĝentilan sintenon. </p>

<p> Por uzi bestkunsenton la besto kaj la druido kutime ne rajtas esti pli malproksime ol 9 metrojn por tio kaj la interrilato daŭras ĉiAKaŭ unu minuton. Sed same kiel kun homoj tio povas daŭri pli aŭ malpli longe. </p>

<p> Per tiu ebleco la druido ankaŭ povas provi influi magiajn bestojn kiuj havas inteligentecon de 1 aŭ 2. Tiam li ricevas malbonon de -4.

<p id="trapasi-subkreskajxon"><b>Trapasi subkreskaĵon (EE):</b> Ekde la 2a nivelo la druido povas per plena moviĝrapideco trapasi ĉiun specon de subkreskaĵoj (kiel naturaj dornoj, arbustaro, sovaĝa pejzaĝo kaj simila tereno) sen riski vundojn aŭ malhelpon. Dornoj kaj alia subkreskaĵo kiuj estas magie modifitaj por malhelpi la trapason koncernas la druidon kiel kutime. </p>

<p id="pasxi-sen-piedsigno"><b>Paŝi sen piedsigno (EE):</b> Ekde la 3a nivelo la druido ne postlasas piedsignon en natura ĉiAKaŭaĵo kaj ne povas esti sekvata per spurlegado. Li tamen povas decidi postlasi piedsignojn. </p>

<p id="rezisti-logon-de-la-naturo"><b>Rezisti logon de la naturo (EE):</b> Ekde la 4a nivelo la druido ricevas la bonon +4 por savĵetoj kontraŭ sorĉsimilaj kaj supernaturaj eblecoj de feoestaĵoj. Tiu bono ankaŭ aplikiĝas ĉe sorĉoj kiuj rilatas al plantoj kiel kreskigi dornojn, senpluveco, kurbigi lignon, ĉiAKaŭvolvi. </p>

<p id="zooformo"><b>Zooformo (SNE):</b> Je la 4a nivelo la druido ricevas la eblecon transformiĝi unu fojon tage al malgranda aŭ mezgranda besto kaj reen. Por tio li povas elekti ĉiun beston kiu estas indikita kiel besto. La ebleco funkcias krom la sekvantaj esceptoj kiel la sorĉo zooformo I. La transformiĝo daŭras unu horon por ĉiu druidonivelo se li ne antaŭe retransformiĝas. La transformiĝo (al besto aŭ reen) estas bazago kaj ne provokas okazatakojn. La druido nur povas elekti zooformon kiun li konas. </p>
<p> En zooformo la druido perdas sian eblecon paroli kaj estas limigita al la sonoj de la netrajnita besto. Tamen li povas inteAKomunikiĝi kun aliaj bestoj de sia nova formo. (La natura sono de papago estas krio, tiel la druido ankaŭ ne povas paroli en tia formo.) </p>
<p>La druido povas uzi la eblecon unu plian fojon je la 6a nivelo kaj poste je ĉiu plia dua nivelo ĝis maksimumo de 8 fojoj je la 18a nivelo. Je la 20a nivelo la druido povas ĉiam ajn decidi ŝanĝi sian formon. Je pli altaj niveloj li ankaŭ povas transformiĝi al pli grandaj kaj pli malgrandaj specioj kaj eĉ al plantoj kaj elementuloj. Ĉiu transformiĝo sendepende de la formo foruzas unu aplikon de la ebleco. </p>
<p>Je la 6a nivelo la druido povas transformiĝi al granda aŭ tre malgranda besto aŭ al malgranda elementulo. Se li elektas zooformon, la ebleco funkcias kiel la sorĉo zooformo II kaj se li elektas formon de elementulo, ĝi funkcias kiel formo de elementulo I.</p>
<p> Je la 8a nivelo li povas transformiĝi al grandega aŭ malgrandega besto. Li ankaŭ povas elekti la formon de mezgranda elementulo aŭ malgranda aŭ mezgranda plantokreaĵo. La ebleco rilatas laŭ formo al zooformo III, formo de elementulo II aŭ plantoformo I. </p>
<p> Je la 10a nivelo la druido povas elekti formon de granda elementulo aŭ granda plantokreaĵo. Se li elektas elementulon, la ebleco funkcias kiel formo de elementulo III kaj se li elektas planton, ĝi funkcias kiel plantoformo II. </p>
<p> Je la 12a nivelo la druido povas elekti formon de grandega elementulo aŭ grandega plantokreaĵo. Se li elektas elementulon, la ebleco funkcias kiel formo de elementulo IV kaj se li elektas planton, ĝi funkcias kiel plantoformo III. </p>

<p id="imuneco-kontraux-venenoj"><b>Imuneco kontraŭ venenoj (EE):</b> Ekde la 9a nivelo la druido estas imuna kontraŭ ĉiu veneno. </p>

<p id="mil-vizagxoj"><b>Mil vizaĝoj (SNE):</b> Ekde la 13a nivelo la druido povas ĉiumomente ŝanĝi sian aspekton. Tiu ebleco funkcias kiel la sorĉo ŝanĝi figuron. Li povas fari tion nur en sia natura formo. </p>

<p id="sentempa-korpo"><b>Sentempa korpo (EE):</b> Ekde la alveno al la 15a nivelo la atributoj de la druido ne plu malboniĝas rilate al maljuniĝo. Li ankaŭ ne povas maljuniĝi pro magiaj efikoj. Ŝanĝoj kiuj okazis antaŭe ne reŝanĝas. Bonoj pro la aĝo li tamen ricevas kaj li mortas se la tempo venas. </p>

<h2>Estintaj druidoj</h2>

<p> Druido kiu ĉesas adori la naturon, ŝanĝiĝas al malpermesita pensmaniero aŭ instruas la druidan al nedruido perdas siajn sorĉojn, siajn druidokapablecojn kaj sian bestkunulon. La sperto kun armiloj kaj armaĵoj ne ŝanĝiĝas. Li ne plu povas altiĝi kiel druido ĝis li pentofaras (vidu la sorĉon pentofaro). </p>

<h2 id="bestkunuloj">Bestkunuloj</h2>

<p> La eblecoj de la bestkunulo dependas de la nivelo de la druido kaj de la popolokarakterizaĵoj de la besto. La malsupra tabelo enhavas la bazajn ludvalorojn de bestkunulo. Bestkunuloj restas kreaĵoj de la monstrospeco besto kiam temas pri kiuj sorĉoj havas efikon al ili. </p>

<p><b>Klasnivelo:</b> Tio estas la druidonivelo de la rolulo. Al la druidonivelo sumiĝas ĉiuj niveloj de klasoj kiuj havas bestkunulon por determini la eblecojn de la bestkunulo. </p>

<p><b>VK:</b> Tio estas la nombro da okflankaj (k8) vivkubetoj kiujn havas la bestkunulo. Al ĉiu aldoniĝas la KON-modifilo kiel kutime. </p>

<p><b>BAB:</b> Tio estas la bazataka bono de la bestkunulo. La bazataka bono de la bestkunulo egalas al la bazataka bono de druido de la nivelo kiu egalas la vivkubetojn (VK) de la bestkunulo. Bestkunuloj ne ricevas aldonajn atakojn por altaj bazatakaj bonoj ĉe atakoj per siaj naturaj armiloj. </p>

<p><b>REF/VOL/PER:</b> Tiuj estas la bazaj valoroj por savĵetoj por la bestkunulo. La bestkunulo havas bonajn refleks- kaj persitecoĵetojn. </p>

<p><b>Kapablecoj:</b> Tio priskribas la tutan kvanton da kapablecrangoj kiujn la bestkunulo havas. Bestkunuloj povas distribui tiujn rangojn al ĉiuj kapablecoj kiuj estas indikitaj ĉe bestkapablecoj. Se la bestkunulo altigas sian inteligentecon super 10 li ricevas aldonajn rangojn por kapablecoj kiel kutime. Bestkunuloj kun inteligenteco 3 aŭ pli alta povas distribui rangojn al ajnaj kapablecoj. Bestkunulo ne rajtas havi pli da rangoj en kapableco ol ĝi havas vivkubetojn. </p>

<p><b>Talentoj:</b> Tio estas la tuta kvanto da talentoj kiujn la bestkunulo havas. Bestkunuloj devas elekti iliaj talentoj inter la talentoj kiuj estas indikitaj sub besttalentoj. Ili povas ankaŭ elekti aliajn talentojn sed en kelkaj kazoj ili ne kapablas uzi ilin (kiel ekzemple sperto kun militarmiloj). Krome bestkunuloj ne rajtas elekti talenton kun la kondiĉo de bazataka bono de +1 aŭ pli bone kiel unuan talenton (sed nur ekde la dua talento kiun ili ricevas kiam ili havas 3 vivkubetojn).

<p><b>Natura armaĵo:</b> La nombro kalkuliĝas aldone al la natura ekzistanta armaĵo de la besto. </p>

<p><b>FOR/LER:</b> Aldonu tiun nombron al la atributoj forto kaj lerteco de la besto. </p>

<p><b>Kromaj artifikoj:</b> La valoro en tiu kolumno estas la tuta nombro da kromaj artifikoj kiujn la besto konas apud tiuj kiujn la druido eble instruis al la besto per la kapableco interrilati kun bestoj (vidu la kapablecon interrilati kun bestoj por pliaj detaloj kiel instrui artifikojn al besto). Tiuj aldonaj artifikoj ne eluzas tempon por la instruado aŭ kapablecĵetoj de interrilati kun bestoj kaj ili kalkuliĝas por la tuta nombra da artifikoj kiujn la besto povas lerni. La druido elektas la artifikojn kaj poste ne plu povas ŝanĝi ilin. </p>

<b>Specife:</b> Ĉi tie troveblas eblecoj kiujn akiras la beston kiam ĝia povo altiĝas. La diversaj eblecoj havas priskribojn malsupren. </p>

