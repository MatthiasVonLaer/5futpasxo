<h2>Klasaj karakterizaĵoj</h2>

<p> La sekvantaj eblecoj estas la klasaj karakterizaĵoj de la fripono. </p>

<p><b>Sperto kun armiloj kaj armaĵoj:</b> La fripono havas sperton kun ĉiuj simplaj armiloj kaj aldone kun la <a href="../ekipajxo/#glavo-kurta">glavo kurta</a>, <a href="../ekipajxo/#metalkapa-klabo">metalkapa klabo</a>, <a href="../ekipajxo/#rapiro">rapiro</a>, <a href="../ekipajxo/#arko-kurta">arko kurta</a> kaj <a href="../ekipajxo/#manarbalesto">manarbalesto</a>. La fripono havas sperton kun malpezaj armaĵoj sed ne por ŝildoj. </p>

<p id="embuska-atako"><b>Embuska atako:</b> Se la fripono atakas malamikon kiu ne efike povas defendi sin, li povas per alcela bato al vivgrava korpoparto kaŭzi pli damaĝôn.</p>

<p> La atakoj de la fripono kaŭzas pli da damaĝo ĉiam kiam la malamiko perdas sian lertecbonon por la armaĵklaso (sendepende ĉu li havas LER-bonon) aŭ se la fripono ambaŭflankas sian malamikon. Je la unua nivelo li kaŭzas 1k6 kroman damaĝon. Por ĉiuj pliaj du niveloj kiel fripono, la damaĝo plimultiĝas je 1k6. Se la fripono faras kritikan trafon, la kroma damaĝo ne multiplikiĝas. Distancatakoj nur tiam kalkuliĝas kiel embuskaj atakoj se la celo estas maksimume 9 m de la fripono. </p>

<p> Per armilo kiu kaŭzas nemortigan damĝon (kiel ekz. la <a href="../ekipajxo/#klabo">klabo</a> aŭ <a href="../ekipajxo/#bato-sen-armilo">bato sen armilo</a>), la fripono povas fari embuskan atakon kiu kaŭzas nemortigan damaĝon anstataŭ mortigan. Per armilo kiu kaŭzas mortigan damaĝon, tio ne eblas, eĉ se li prenas la kutiman malbonon de -4. </p>

<p> Por fari embuskan atakon la fripono devas ekzakte vidi sian celon, por trovi malfortaĵojn de la malamiko kaj li devas kapabli atingi ilin. Li ne povas fari embuskajn atakojn se la celo havas kamuflaĵon. </p>

<p id="trovi-kaptilojn"><b>Trovi kaptilojn:</b> La fripono aldonas sian duonan fripononivelon al kapabelecĵetoj de percepto por trovi kaptilojn kaj por ĵetoj de malfunkciigi mekanismon (minimumo +1). La fripono povas uzi la kapablecon malfunkciigi mekanismon por malfunkciigi magiajn kaptilojn. </p>

<p id="sinsavi"><b>Sinsavi (EE):</b> Ekde la 2a nivelo la fripono povas tre lerte eviti magiajn kaj nekutimajn atakojn. Se li sukcese faras refleksoĵeton kontraŭ atako, kiu kutime kaŭzas duonan damaĝon ĉe sukcesa refleksoĵeto, li suferas neniun damaĝôn. Li nur povas sinsavi se li ne havas armaĵon aŭ se li portas malpezan vestaĵon. Senkapabla fripono ne povas sinsavi. </p>

<p id="ruzajxoj"><b>Ruzaĵoj:</b> Kiam la fripona fariĝas pli sperta, li lernas ruzaĵojn kiuj helpas al li konfuzi siajn malamikojn. Je la 2a nivelo la fripono lernas sian unuan ruzaĵon. Por ĉiuj pliaj du niveloj de fripono li povas lerni novan ruzaĵon. La fripono ne povas lerni la saman ruzaĵon dufoje. </p>

<p> Ruzaĵoj kiuj estas indikitaj kun asterisko<sup>*</sup>, aldonas efikojn al la embuska atako de la fripono. Nur unu el tiuj efikoj povas esti aplikata kaj la fripono devas fari la decidon antaŭ li faras la atakĵeton. </p>

<ul>
    <li><em>Alta magio (SSE):</em> La fripono ricevas la eblecon efektivigi unu sorĉon de la unua nivelo de la sorĉlisto de magiisto / sorĉisto. Li povas apliki la sorĉon dufoje dum unu tago kiel sorĉosimila ebleco. La sorĉonivelo egalas lian fripononivelon. La MFG por la savĵeto kontraŭ tiu sorĉo estas 11 + INT-modifilo de la fripono. La fripono devas havi inteligentecon de minimume 11 por elekti tiun ruzaĵon. La fripono devas kapabli la ruzaĵon malalta magio por elekti tiun ruzaĵon. </li>
    <li><em>Armilotrejnado (EE):</em> Fripono kiu elektas tiun ruzaĵon, ricevas la talenton armilofokuson kiel kroma talento. </li>
    <li><em>Batalruzaĵo:</em> Fripono kiu elektas tiun ruzaĵon, ricevas bataltalenton por kiu li plenumas la kondiĉojn kiel kroma talento (vidu ĉapitron <a href="../talentoj">Talentoj</a>). </li>
    <li><em>Fripona ruzeco (EE):</em> Fripono kiu elektas tiun ruzaĵon, ricevas la talenton armiloruzecon kiel kroma talento. </li>
    <li><em>Kaptilosento (EE):</em> Kiam fripono kun tiu ruzaĵo eniras ĉirkaŭaĵon de 3 m de kaptilo, li ricevas subitan kapablecĵeton de percepto por trovi la kaptilon. Tiu ĵeto devus esti farata de la ludgvidanto. </li>
    <li><em>Malalta magio (SSE):</em> La fripono ricevas la eblecon efektivigi unu sorĉartifikon (sorĉo de la nula nivelo) de la sorĉlisto de magiisto / sorĉisto. Li povas apliki la sorĉon trifoje dum unu tago kiel sorĉosimila ebleco. La sorĉonivelo egalas lian fripononivelon. La MFG por la savĵeto kontraŭ tiu sorĉo estas 10 + INT-modifilo de la fripono. La fripono devas havi inteligentecon de minimume 10 por elekti tiun ruzaĵon. La fripono devas kapabli la ruzaĵon malalta magio por elekti tiun ruzaĵon. </li>
    <li><em>Malrapidigi reagojn<sup>*</sup> (EE):</em> Malamiko kiu ricevas damaĝon per embuska atako de la fripono, ne povas fari okazatakojn dum unu rondo. </li>
    <li><em>Rapida rampado (EE):</em> Se la fripono kuŝas surtere, li povas rampi per sia duona moviĝorapideco. Tiu moviĝo provokas okazatakojn kiel kutima moviĝo. Se la fripono rampas, li povas fari kvinfut-paŝon. </li>
    <li><em>Rapida sekreteco (EE):</em> Fripono kun tiu ruzaĵo povas moviĝi per plena moviĝorapideco se li uzas la kapablecon sekreteco sen sankcioj por la ĵeto. </li>
    <li><em>Rapide malfunkciigi (EE):</em> La fripono nur bezonas la duonon de la tempo por malfunkciigi kaptilojn per la kapableco malfunkciigi mekanismon (minimume unu rondo). </li>
    <li><em>Rezistkapablo (EE):</em> Unufoje tage la fripono povas ricevi portempajn vivpoentojn kiuj egalas lian fripononivelon. Uzi tiun ruzaĵon estas subita ago. Ĝi nur povas esti aplikata kiam la vivpoentoj de la fripono falas sub nul. Tiu ebleco povas esti aplikata por savi la rolulon de la morto kaj de la stato mortanta. La portempaj vivpoentoj daŭras unu minuton. Se la vivpoentoj de la fripono falas sub nul pro la perdo de la portempaj vivpoentoj, li perdas la konscion kaj estas mortanta. </li>
    <li><em>Sanganta vundo<sup>*</sup> (EE):</em> Fripono kiu kapablas tiun ruzaĵon, povas kaŭzi sangantan vundon ĉe vivanta malamiko per embuska atako. La celo de la atako ricevas po unu poento da damaĝo por ĉiu plia kubeto kiun la fripono ricevas pro la embuska atako (ekzemple 4k6 egalas sangadon de 4 poentoj). La sanganta malamiko ricevas la damaĝon je la komenco de sia vico. La sangado povas esti ĉesigata per kapablecĵeto de kuraci (MFG 15) aŭ per ĉiu efiko kiu sanigas vivpoentojn. La damaĝo de pliaj sangantaj vundoj ne sumiĝas. Damaĝo de sangado preteriras ĉiun damaĝoredukton, kiun povas havi kreaĵon. </li>
    <li><em>Ŝnurdancisto (EE):</em> Tiu ruzaĵô ebligas al fripono, moviĝi sur mallarĝa grundo per plena moviĝorapideco. Por tio li uzas la kapablecon akrobataĵon sen sankcioj por la ĵeto. Krome li ne kalkuliĝas kiel sur la malĝusta piedo se li uzas akrobataĵon sur mallarĝa grundo. </li>
    <li><em>Suprensalti (EE):</em> La fripono povas per libera ago ekstari. Li tamen ankoraŭ provokas okazatakon se li ekstaras en kampo kiu estas en la trafopovo de malamiko. </li>
    <li><em>Surprizatako (EE):</em> Dum la surprizorondo la fripono kaptas ĉiujn malamikojn sur la malĝusta piedo, eĉ se ili jam agis en tiu rondo. </li>
</ul>

<p id="kaptilsensiveco"><b>Kaptilsensiveco (EE):</b> Ekde la tria nivelo la fripono havas intuician sensivecon por kaptiloj. Li ricevas bonon de +1 por refleksĵetoj por evitmoviĝi de kaptiloj kaj evitbonon de +1 por sia AK kontraŭ atakoj de kaptiloj. Tiu bono altiĝas al +2, kiam la fripono alvenas ĉe la 6a nivelo, al +3 ĉe la 9a nivelo, al +4 ĉe la 12a nivelo, al +5 ĉe la 15a nivelo kaj al +6 ĉe la 18a nivelo. Bonoj por kaptilsensiveco de diversaj klasoj sumiĝas. </p>

<p id="refleksmovo"><b>Refleksmovo (EE):</b> Ekde la 4a nivelo la fripono povas reagi al danĝeroj antaŭ liaj sensoj kutime permesus tion. Li ne plu povas esti surprizita sur la malĝusta piedo kaj ne perdas sian lertecbonon por la AK kiam la atakanto estas nevidebla. Li tamen daŭre perdas sian LER-bonon se li estas nemovkapabla. Fripono kun tiu ebleco ankaŭ povas perdi sian LER-bonon se lia malamiko sukcesa faras finton kontraŭ li (vidu ĉapitron <a href="../batalo">Batalo</a>). Se la fripono jam havas refleksmovon de alia klaso, li aŭtomate ricevas la eblecon refleksmovon progresitan (vidu malsupren). </p>

<p id="refleksmovo-progresita"><b>Refleksmovo progresita (EE):</b> Fripono de la 8a nivelo aŭ pli alta nivelo ne plu povas esti ambaŭflankata. </p>

<p> Tiu defendo malebligas ke alia fripono uzas sian embuskan atakon se la fripono estas ambaŭflankata, krom se la fripononivelo de la atakanto estas kvar nivelojn pli alta. </p>

<p> Se fripono jam havas la eblecon refleksmovon, li aŭtomate ricevas la eblecon refleksmovon progresitan kaj liaj niveloj de fripono sumiĝas kun la niveloj de aliaj klasoj, kiuj donas al li refleksmovon, por determini lian nivelon se temas pri ambaŭflanki aŭ esti ambaŭflankata. </p>

<p id="ruzajxoj-progresitaj"><b>Ruzaĵoj progresitaj:</b> Je la 10a nivelo kaj je ĉiu plia dua nivelo la fripono povas lerni ruzaĵon progresitan anstataŭ ruzaĵon. </p>

<ul>
    <li><em>Protektsorĉa bato<sup>*</sup> (EE):</em> Malamikoj kiuj ricevas damaĝon per embuska atako, ankaŭ estas la celo de protektsorĉa magio kontraŭ la sorĉo de la plej malalta nivelo kiu estas sur la malamiko. La sorĉnivelo por tiu ebleco egalas la nivelon de la fripono. Por lerni la protektsorĉan baton la fripono devas scipovi la ruzaĵon altan magion. </li>
    <li><em>Evita ruliĝo (EE):</em> Ĉe ebla mortiga bato la fripono povas moviĝi por ricevi malpli damaĝon. Unufoje tage se li estus reduktita al nul aŭ malpli da vivpoentoj per atako en la batalo (per armilo aŭ alia bato, sed ne per sorĉo aŭ sorĉsimila ebleco), li povas fari refleksĵeton. La MFG egalas la damaĝon. Se sukcesas li nur ricevas duonan damaĝon. Se malsukcesas li ricevas tutan damaĝon. La fripono devas konscii pri la atako kaj esti movkapabla por uzi tiun eblecon. Se la fripono perdas sian LER-modifilon de la AK, li ne povas fari evitan ruliĝon. Pro tio ke oni kutime ne rajtas fari refleksĵeton por duona damaĝo, la fripono ne povas uzi la eblecon sinsavi. </li>
    <li><em>Glita menso (EE):</em> Tiu ruzaĵo reprezentas la eblecon de la fripono eliri el magiaj efikoj, kiuj provas kontroli lin. Se la fripono estas la celo de transsorĉado kaj ne sukcesas kun la savĵeto, li povas dum sia venonta vico denove provi la ĵeton kun sama MFG. Li nur ricevas unu ŝancon eliri el la transsorĉado. </li>
    <li><em>Kapablecmajstreco (EE):</em> La fripono estas tiel lerta ĉe certaj kapablecoj ke li ankaŭ povas fidinde utiligi ilin en stresigaj situacioj. La fripono povas elekti (3 + INT-bono) kapablecoj. Por tiuj kapablecoj li ĉiam povas uzi preni 10, eĉ se streso aŭ aliaj eksteraj ĉirkumstancoj malpermesus tion. Li povas lerni tiun ruzaĵon plioble kaj elekti ĉiufoje aliajn kapablecojn. </li>
    <li><em>Malfortiga bato<sup>*</sup> (EE):</em> Fripono kun tiu ruzaĵo povas fari embuskajn atakojn tiel precize ke liaj malamikoj malfortiĝas kaj handikapiĝas. Malamiko kiu ricevas damaĝon per embuska atako, ricevas plie du poentojn da portempa FOR-damaĝo. </li>
    <li><em>Sinsavi progresite (EE):</em> Tiu ebleco funkcias kiel sinsavi kun la diferenco ke la fripono eĉ nur ricevas duonan damaĝon se la savĵeto malsukcesas. Se la fripono estas senkapabla, li ne povas uzi sinsavi progresite. </li>
    <li><em>Talento (EE):</em> Anstatŭ ruzaĵon la fripono povas elekti talenton laŭ sia elekto, por kiu li plenumas la kondiĉojn. </li>
    <li><em>Uzi okazon (EE):</em> Unufoje dum rondo la fripono povas fari okazatakon kontraŭ malamikoj, kiuj ĵus estas trafitaj en proksimbatalo. Tiu atako kalkuliĝas kiel okazatako. Eĉ se la fripono havas batalrefleksojn li nur povas uzi tiun eblecon unufoje en rondo. </li>
</ul>

