<title>Ekludi</title>

<h1>Ekludi</h1>

<h2>Ludi la ludon</h2>

<img src="../../bildoj/insulo_hela.jpg" style="width:55%;">

<p> Kiam vi ludas la ludon, la ludgvidanto priskribas la okazaĵojn en la ludmondo kaj la ludantoj priskribas unu post la alia kiel iliaj roluloj reagas. Kontraŭe al kutimaj rakontoj, ne estas fiksaj la agoj de roluloj kiuj estas kontrolataj de la ludgvidanto (nomitaj neludantroluloj, mallonge NLR). Por plejmultaj agoj vi devas kubĵeti por malkovri ĉu ili estis sukcesaj aŭ ne kaj kelkaj estas pli malfacilaj ol aliaj. Ĉiu rolulo havas fortojn kaj malfortojn kaj pro tio li ricevas bonojn aŭ malbonojn kiuj dependas de liaj kapablecoj kaj eblecoj. </p>

<p> Ĉiam kiam kubĵeto estas bezonata, tio estas indikita per "k#". La "#" reprezentas la nombro da flankoj de la kubeto per kiu necesas ĵeti. Se vi devas ĵeti plurajn kubetojn, tion indikas nombro kiu troveblas antaŭ la "k". Ni supozu ke vi devas ĵeti 4k6, do vi ĵetas kvar sesflankajn kubetojn kaj sumigas la rezultojn. Kelkfoje post tiu indiko troveblas + aŭ - kun iu nombro. Tiam vi devas adicii aŭ subtrahi tiun nombron de la tuta rezulto (ne de ĉiu kubeto). Por plejmultaj ĵetoj oni uzas k20 plus modifiloj kiuj dependas de la kapablecoj aŭ eblecoj kaj de la situacio. Ĝenerale estas pli bone ĵeti altajn nombrojn. Elcentĵetoj estas speciala kazo, ili estas indikitaj per "k%". Por ĵeti hazardan elcentnombron, vi povas ĵeti du malsamkolorajn dekflankajn kubetojn (2k10). Unu el ili reprezentas la dekoj. Ĵetu ambaŭ kubetojn. Se la kubeto por la dekoj montras "4" kaj la alia montras "2", vi ĵetis 42. Se la kubeto por la dekoj montras "0" la rezulto estas inter 1 kaj 9 aŭ 100 se ambaŭ estas nul. Kelkaj k10 ankaŭ estas surprintitaj per "10", "20", "30" ktp. por faciligi la elcentĵeton. Se ne estas alie indikita oni ĉiam rondigas malsupren. </p>

<p> Kiam via rolulo ekvojaĝos por aventuroj, li kolektos oron, magiajn objektojn kaj spertpoentojn. Per la mono li povas aĉeti pli bonan ekipaĵon kaj la magiaj objektoj havas potencajn eblecojn kiuj plibonigas vian rolulon. Spertpoentoj estas disdonataj pro superitaj defioj kaj pro progresado en la ludagado. Kiam via rolulo akiris sufiĉe da spertpoentoj, li altiĝas je unu nivelo kaj li ricevas novajn fortojn kaj eblecojn, kiuj helpas lin por majstri eĉ pli grandajn defiojn. </p>

<h2>La plej grava regulo</h2>

<p> La reguloj ĉi tie celas vivigi la rolulojn kaj la ludmondon. Ili estas tiel faritaj por ke ili estu facile uzeblaj kaj por ke ili ebligu ekscitan ludon. Tamen povas okazi ke kelkaj ne kongruas kun la ludmaniero kiun vi preferas en via grupo. Ne forgesu ke tiuj reguloj estas viaj. Vi povas ŝanĝi ilin por adapti ilin laŭ viaj bezonoj. Plejmultaj ludgvidantoj havas siajn "hejmregularojn". La ludgvidanto kaj la ludantoj devus ĉiam interkonsenti pri tiaj rugulŝanĝoj por ke ĉiu komprenu kiel ili efikas en la ludo. La ludgvidanto ja havas la lastan vorton pri la reguloj sed la ludo devus esti komuna sperto kaj ĉiu devus kontribui siajn ideojn, kiam oni diskutas pri iu regulo. </p>

<h2>Ĝeneralaj terminoj</h2>

<p> En la ludo estas kelkaj ĝeneralaj terminoj, mallongigoj kaj difinoj, kiuj volas helpi al vi kompreni la ludon. La sekvantaj estas la plej kutimaj: </p>

<p><b>Ago:</b> Ago estas difinita tempodivido en batalovico. Dum la ago vi povas utiligi viajn kapablecojn, apliki sorĉojn aŭ ataki. Estas diversaj agotipoj: bazago, movago, libera ago kaj plena ago (vidu pli en <a href="../batalo">Batalo</a>).</p>

<p><b>Akumula:</b> Akumula signifas ke certaj bonoj aŭ malbonoj sumiĝas por certaj ĵetoj aŭ valoroj. Kutime bonoj de sama tipo ne estas akumulaj. Valoras la plej alta. Plejmulto da malbonoj estas akumulaj, do ili sumiĝas. Tamen malbonoj kune kun bonoj estas akumulaj. Tio signifas ke malbonoj povas neniigi parte aŭ tute bonojn (povas ankaŭ esti ke la malbono estas pli granda ol la bono, kio rezultas entute kiel malbono por la ĵeto aŭ la valoro). </p>

<p><b>Armaĵklaso (AK):</b> Ĉiuj kreaĵoj en la ludo havas armaĵklason. Tio indikas kiel malfacile estas trafi tiun kreaĵon. Kiel ĉiam ju pli alta des pli bona. </p>

<p><b>Atributvaloro:</b> Ĉiu kreaĵo havas 6 atributvalorojn: forto, lerteco, konstitucio, inteligenteco, saĝeco kaj karismo. Tiuj valoroj reprezentas la plej bazajn ecojn de la kreaĵo. Ju pli alta la valoro des pli kapabla estas la rolulo en tiu branĉo.</p>

<p><b>Batalmanovra bono (BMB):</b> Tiu valoro indikas kiel bonan sperton kreaĵo havas por fari batalmanovron. Tiu valoro aldoniĝas al ĵeto de k20, kiam rolulo volas fari batalmanovron.</p>

<p><b>Batalmanovra defendo (BMD):</b> Tiu valoro indikas kiel malfacile estas fari batalmanovron kontraŭ kreaĵo. Kiam vi faras batalmanovron kontraŭ kreaĵon, vi devas uzi lian batalmanovran defendon kiel malfacilecgrado.</p>

<p><b>Batalmanovro:</b> Batalmanovro estas batalago kiu ne rekte damaĝas vian malamikon - ekzemple la provo faligi lin, senarmigi lin, aŭ komenci luktadon.</p>

<p><b>Bazataka bono (BAB):</b> Ĉiu kreaĵo havas bazatakan bonon. Tiu indikas kiel bone la kreaĵo scipovas batali. Kiam rolulo altiĝas je nivelo aŭ je vivkubetoj tiam altiĝas ankaŭ sia bazataka bono. Se la bazataka bono atingas +6, +11 kaj +16, la rolulo povas fari aldonan atakon se li uzas plenan agon (tipo de ago, vidu <a href="../batalo">Batalo</a>).</p>

<p><b>Bono:</b> Bonoj estas valoroj kiuj sumiĝas al ĵetoj kaj rolulvaloroj. La plejmulto da bonoj estas de determinita tipo. Ĝenerale bonoj de la sama tipo ne estas akumulaj kaj oni uzas la pli grandan.</p>

<p><b>Damaĝoredukto (DR):</b> Kreaĵoj kiuj estas rezistemaj kontraŭ damaĝo, havas kutime damaĝoredukton. La indikita nombro estas subtrahata de la damaĝo kiu estas kaŭzita per fizika atako. La plejmulton da tipoj de damaĝoredukto oni povas ĉirkaŭiri per certaj armiloj. Tio estas indikita per la litero "/" post la nombro. Post la oblikva streketo legeblas la armiltipo per kiu oni povas ĉirkaŭiri la damaĝoredukton. Kelkaj tipoj de damaĝoredukto validas por ĉiuj ataktipoj. Tio estas indikita per la litero "-" post la oblikva streketo (vidu la ĉapitron <a href="../eksterordinaraj_eblecoj">Eksterordinaraj eblecoj</a> por pliaj informoj). </p>

<p><b>Eksterordinaraj eblecoj (EE):</b> Ili estas specialaj eblecoj, kiuj estas nemagiaj.</p>

<p><b>Grado:</b> Sorĉoj dividiĝas en gradoj de 0 al 9. La grado montras kiel potenca la sorĉo estas. Kiam sorĉkonantoj altiĝas en nivelo, ili lernas sorĉojn de pli altaj gradoj.</p>

<p><b>Iniciato:</b> Ĉiam kiam batala komenciĝas, ĉiuj partoprenantaj kreaĵoj devas ĵeti iniciatan ĵeton por determini la batalvicon. Ju pli granda la ĵeto des pli frua la kreaĵo povas agi.</p>

<p><b>Ĵeto:</b> Kutime signifas ĵeton de k20, al kiu aldoniĝas aliaj modifiloj. Tipaj ĵetoj estas atakĵetoj, kapablecĵetoj kaj savĵetoj. </p>

<p><b>Kapableco:</b> Kapableco estas normala ebleco, kiel ekzemple grimpi sur muron, kaŝiri en silenta koridoro aŭ percepti entrudiĝanton. La kvanto da rangoj en iu kapableco indikas kiel bonan sperton la kreaĵo havas en tiu kapableco. Kiam kreaĵoj ricevas novajn vivkubetojn, ili ankaŭ ricevas novajn kapablecrangojn, kiujn ili povas dispartigi al siaj kapablecoj. </p>

<p><b>Klaso:</b> La klaso reprezentas la profesion de kreaĵo aŭ rolulo. Per la klaso roluloj ricevas bonojn kaj ili povas efektivigi agojn kiujn oni kutime ne kapablas kiel sorĉi aŭ figurŝanĝi. Se kreaĵo gajnas novajn nivelojn en iu klaso, ĝi ankaŭ ricevas novajn kaj pli potencajn eblecojn. Plejmulto da LR havas nivelojn en la bazklasoj ĉar ili estas la plej potencaj (vidu <a href="../klasoj">Klasoj</a>). Plejmulto da NLR altiĝas en NLR-klasoj. Tiuj ne estas tiel potencaj (vidu <a href="../krei_nlr">Krei NLR</a>). </p>

<p><b>Koncentriĝoĵeto:</b> Se kreaĵo efektivigas sorĉon, sed dume estas perturbata, ĝi devas fari koncentriĝoĵeton por ke la sorĉo ne perdiĝu (vidu <a href="../magio">Magio</a>). </p>

<p><b>Kreaĵo:</b> Kreaĵoj estas estaĵoj kiuj aktive partoprenas al la mondo aŭ al la rakonto. Ĉiuj LR, NLR kaj monstroj estas kreaĵoj. </p>

<p><b>Ludantrolulo (LR):</b> Estas tiuj figuroj kiuj estas ludataj de la ludantoj. </p>

<p><b>Ludgvidanto (LG):</b> La ludgvidanto estas tiu kiu kontrolas ĉiujn elementojn de la rakonto kaj de la mondo, kiun la ludantoj esploras. La ludgvidanto devas ĉiam provi esti justa kaj prizorgi ke la ludo estas plaĉema. </p>

<p><b>Malbono:</b> Malbonoj estas valoroj, kiuj estas subtrahataj de ĵetoj aŭ rolulvaloroj. Malbonoj ĝenerale ne havas tipon kaj estas akumulaj. </p>

<p><b>Malfacilecgrado (MFG):</b> Ĉiam kiam kreaĵo volas fari agon kies sukceso ne certas, ĝi devas fari ĵeton (vidu ĉapitron <a href="../kapablecoj">Kapablecoj</a>). La rezulto devas esti minimume tiom granda kiel la indikita malfacilecgrado, por ke la ago estu sukcesa. </p>
<p><b>Monstro:</b> Monstroj estas kreaĵoj kies fortoj kaj eblecoj dependas de iliaj vivkubetoj rilataj al iliaj popoloj kaj ne al iliaj klasniveloj. Tamen estas monstroj kiuj havas klasnivelojn. LR ĝenerale ne estas monstroj. </p>

<p><b>Multipliki:</b> Ĉiam se vi devas apliki pli ol unu multiplikanton, tiuj multiplikantoj estas akumulaj sed ne multiplikiĝas. Anstataŭe vi adicias la du multiplikantojn kaj subtrahas la rezulton per unu. Ni supozu ke vi dufoje devas apliki la multiplikanton &times;2. Tiam vi devas apliki entute &times;3 kaj ne &times;4. </p>

<p><b>Neludantrolulo (NLR):</b> Neludantroluloj estas roluloj kiuj estas kontrolataj de la ludogvidanto (LG). NLR havas kutime nivelojn en klasoj. </p>

<p><b>Nivelo:</b> La nivelo reprezentas la potencon de rolulo. Estas du niveltipoj. La klasnivelo indikas kiom da niveloj la rolulo havas en certa klaso. La rolulnivelo estas la sumo de ĉiuj klasniveloj kiujn la rolulo posedas en ĉiuj siaj klasoj. </p>

<p><b>Pensmaniero (PM):</b> La pensmaniero priskribas la bazajn moralajn kaj etikajn ideojn de iu kreaĵo. Ĉiu pensmaniero konsistas el du komponantoj. Unu komponanto indikas ĉu la rolulo estas laŭleĝa, neŭtrala aŭ ĥaosa. La alia sekvanta komponanto indikas ĉu la rolulo estas bona, neŭtrala aŭ malbona. Pensmanieroj mallongiĝas per la unua litero de ambaŭ komponantoj, ekzemple LN por laŭleĝa neŭtrala aŭ ĤM por ĥaosa malbona. Kreaĵoj kiuj estas ambaŭmaniere neŭtralaj estas indikataj per N.</p>

<p><b>Rondo:</b> La batalo dividiĝas en rondoj. Dum unu rondo ĉiuj kreaĵoj havas la ŝancon por agi laŭ la ordo de iliaj iniciatoj. Unu rondo egalas al ĉirkaŭ 6 sekundoj en la ludmondo. </p>

<p><b>Savĵeto:</b> Se kreaĵo estas la celo de danĝera sorĉo aŭ efiko, ĝi ofte rajtas fari savĵeton por malgravigi la rezulton de la sorĉo aŭ la efiko. Savĵetoj estas pasivaj ĵetoj - la rolulo ne devas fari agon por la savĵeto - ili fariĝas aŭtomate. Estas tri tipoj da savĵetoj: persistecĵetoj (por rezisti al venenoj, malsanoj kaj aliaj fizikaj minacoj), refleksĵetoj (por eviti efikojn kiuj trafas ampleksan spacon, kiel fajroglobo) kaj voloĵetoj (por rezisti al mensaj atakoj de sorĉoj). </p>

<p><b>Sorĉo:</b> Sorĉoj estas magiaj efikoj, kiuj estas kaŭzitaj de kreaĵoj kiuj povas sorĉi. Sorĉoj povas havi diversajn efikojn, ili povas damaĝi malamikojn sed ankaŭ revivigi mortintajn. Ĉe la sorĉoj estas ĉiam indikita kiun efikon ili havas kaj kiel oni povas rezisti ilin. </p>

<p><b>Sorĉnivelo:</b> La sorĉnivelo indikas kiel potenca estas la sorĉoj de kreaĵo. Multaj sorĉoj havas multajn variablojn kiel la trafodistanco kaj la damaĝo. Tiuj baziĝas sur la sorĉnivelo. </p>

<p><b>Sorĉsimilaj eblecoj (SSE):</b> Sorĉsimilaj eblecoj funkcias kiel sorĉoj, sed apartenas al kreaĵoj per iu eco, iu klasebleco aŭ per la popolo. Kontraŭe al sorĉoj kiujn klasoj de sorĉkonantoj ricevas je nivelaltiĝo. </p>

<p><b>Sorĉrezisto (SR):</b> Kelkaj kreaĵoj estas rezistemaj kontraŭ magio kaj pro tio havas sorĉreziston. Se tiu kreaĵo estas la celo de sorĉo, la sorĉanto devas fari sorĉnivelĵeton por determini ĉu la sorĉo efektivigas. La MFG de la ĵeto egalas la sorĉreziston de la kreaĵo (kelkaj sorĉoj tamen preteriras SR). </p>

<p><b>Spertpoentoj (SP):</b> Kiam rolulo superas defiojn, venkas monstrojn kaj sukcese finas aventurojn, li ricevas spertpoentojn. Kiam ili superas difinitan valoron, la rolulo altiĝas en sia nivelo.</p>

<p><b>Supernaturaj eblecoj (SNE):</b> Supernaturaj eblecoj estas atakmanieroj, defendmanieroj aŭ aliaj magiaj ecoj. Povas esti ke tiuj eblecoj estas ĉiam aktivaj aŭ ke ili devas esti aplikata per speciala ago. Ĉe la priskriboj de la agoj estas indikita la efiko kiun ili havas kaj kiel oni aplikas ilin </p>

<p><b>Talento:</b> Talento estas sperto aŭ ebleco kiun la kreaĵo majstras. Talentoj ofte ebligas ĉirkaŭiri certajn regulojn aŭ restriktojn. Kutime la kreaĵo ricevas siajn talentojn rilate al siaj vivkubetoj. Tamen ankaŭ estas klasoj aŭ certaj eblecoj kiuj donas kromajn talentojn. </p>

<p><b>Vico:</b> En batalrondo ĉiu kreaĵo povas agi laŭ vico. Dum la vico de kreaĵo, ĝi povas fari unu bazagon, unu movagon, unu rapidan agon kaj kelkajn liberajn agojn. Alternative ĝi ankaŭ povas fari unu plenan agon, unu rapidan agon kaj kelkajn liberajn agojn. </p>

<p><b>Vivkubetoj (VK):</b> La vivkubetoj reprezentas la potencon kaj la talenton de kreaĵo. Aliflanke monstroj havas vivkubetojn de sia popolo, kio reprezentas iliajn kapablojn kaj eblecojn. Vivkubetoj estas indikita per la nombro kiom la kreaĵo posedas kaj la tipo de la kubeto, ekzemple 3k8. Tiu valoro estas uzata por determini la tutajn vivpoentojn de la kreaĵo. En tiu ekzemplo la kreaĵo havas 3 vivkubetojn. Por determini la vivpoentojn oni ĵetus trifoje k8 kaj sumigas la rezultojn kune kun aliaj modifiloj. </p>

<p><b>Vivpoentoj (VP):</b> La vivpoentoj difinas en abstrakta formo kiel persistema kaj sana kreaĵo estas tiumomente. Vundoj reduktas la vivpoentojn kaj sanigo (kaj magia kaj natura) redonas vivpoentojn. Kelkaj eblecoj kaj sorĉoj donas portempajn vivpoentojn kiuj malaperas post kelka tempo. Se vivpoentoj de kreaĵo iras sub nul, ĝi perdas la konscion. Se la vivpoentoj malsupreniras al la valoro kiu egalas la negativan konstituciovaloron de la kreaĵo, ĝi mortas. </p>

<h2 id="kiel-krei-rolulon">Kiel krei rolulon?</h2>
<p>Eblas krei ĉiun tipon de rolulo kiun vi volas roli de la ruza ŝtelisto ĝis la brava paladino. Se vi evoluigas rolulon, komencu kun lia koncepto. Ĉu vi volas ludi rolulon kiu ĵetas sin rekte en la batalo kun teruraj monstrojn, kiu impetas kontraŭ ungegoj kaj dentoj per glavo kaj ŝildo? Aŭ ĉu vi preferas ludi misteran rolulon kiu ricevas siajn fortojn el la granda transejo por efektivigi siajn celojn? Preskaŭ ĉio eblas.</p>
<p>Se vi havas ĝeneralan koncepton, vi sekvas la postajn paŝojn por vivigi viajn ideojn. Vi ensrkibas ĉiujn tiujn valorojn de via rolulo en la rolulformularon kiun vi povas elŝuti ĉi tie (<a href="../../elsxutoj/Rolulformularo.pdf">Rolulformularo</a>).
<ul>
    <li><b>1a paŝo - Krei atributvalorojn:</b> Komence vi kalkulas la atributvalorojn (vidu <a href="#elkalkuli-atributvalorojn">Elkalkuli atributvalorojn</a>). Tiuj ses valoroj estas la plej bazaj atributoj de via rolulo kaj gravas por multaj pliaj detaloj de via rolulo. Kelkaj klasoj postulas grandajn certajn atributvalorojn por certaj eblecoj. </li>
    <li><b>2a paŝo - Elektu popolon:</b> Poste vi elektas popolon por via rolulo. Notu la modifilojn por atributvaloroj kaj aliajn popolkarakterizaĵojn (vidu <a href="../popoloj/">Popoloj</a>). Estas sep bazaj popoloj inter kiuj vi povas elekti. Via ludgvidanto tamen povas eventuale aldoni pliajn popolojn. Ĉe ĉiu popolo estas indikita la lingvoj kiujn via rolulo aŭtomate konas kiel kelkaj kromaj lingvoj. La rolulo konas tiom da kromaj lingvoj kiom egalas al lia inteligentecomodifilo.</li>
    <li><b>3a paŝo - Elektu klason:</b> Kiam vi elektis popolon, estas tempo por elekti la rolulklason (vidu ĉapitro <a href="../klasoj">klasoj</a>). La klasoj reprezentas profesion kiel ekzemple batalisto aŭ magiisto. Kiam vi kreas novan rolulon, li komencas je la 1a nivelo. Kiam li akiras spertpoentojn ekzemple por venki monstrojn, li altiĝas je niveloj kaj ricevas novajn fortojn kaj eblecojn. Depende de la klaso povas esti ke li tiam devas fari decidojn, ekzemple elekti novajn sorĉojn. </li>
    <li><b>4a paŝo - Elektu kapablecojn kaj elserĉu talentojn:</b> Poste vi devas kalkuli kiom da kapablecrangoj via rolulo posedas. Tio dependas de lia klaso kaj lia inteligentecmodifilo (kaj kelkaj aliaj bonoj kiel ekzemple la bonoj kiujn homoj ricevas). Poste vi povas dismeti tiujn rangojn al la kapablecoj. Memoru ke vi ne rajtas havi pli da rangoj en unu kapableco ol via rolulo havas nivelojn (ĉe nova rolulo tio kutime estas 1 rango). Post vi elektis viajn kapablecojn vi donas talentojn al via rolulo. Tiuj dependas de lia klaso kaj nivelo. Vi povas elekti talentojn el la listo en ĉapitro <a href="../talentoj">talentoj</a>. </li>
    <li><b>5a paŝo - Aĉetu ekipaĵon:</b> Ĉiu nova rolulo komencas la ludon kun certa kvanto da moneroj oraj kio dependas de lia klaso. Tiu mono povas esti elspezita por diversaj ekipaĵoj, komence de ĉena armaĵo ĝis leda dorsosako. Tiu ekipaĵo helpos vian rolulon dum venontaj aventuroj. Kutime vi ne rajtas aĉeti magiajn objektojn per la komenca mono se via ludgvidanto ne speciale permesas tion. </li>
    <li><b>6a paŝo - La lastaj detaloj:</b> Fine vi determinas la lastajn valorojn kaj ecojn de via rolulo. Liaj vivpoentoj (VP), lia armaĵklaso (AK), liaj savĵetoj, lia iniciatomodifilo kaj liaj atakvalorojn. Ĉiuj tiuj valoroj estas influitaj de la decidoj kiujn vi faris en la pasintaj paŝoj. Krome vi nur devas elpensi nomon kaj pensmanieron por via rolulo kaj difini kiel aspektas via rolulo. Plejbone vi ankaŭ notas kelkajn trajtojn de via rolulo por ke vi pli bone povu roli lin. Aldonaj reguloj troveblas en la ĉapitro <a href="../pliaj_reguloj">pliaj reguloj</a>. </li>
</ul>

<h2>Atributvaloroj</h2>
<p>Ĉiu rolulo havas ses atributvalorojn kiuj priskribas liajn plej bazajn ecojn. Ili reprezentas liajn fundamentajn kompetentecojn kaj eblecojn. La rolulo nur malofte faras kuboĵeton nur sur lia atributvaloro. Tamen tiuj valoroj kaj iliaj rilataj modifiloj influas preskaŭ ĉiun kapablecon kaj eblecon de la rolulo. Atributvaloroj estas kutime etendiĝas de 3 al 18, tamen popolbonoj aŭ -malbonoj povas ŝanĝi tion. Mezkvantaj valoroj egalas al 10. </p>

<h3 id="elkalkuli-atributvalorojn">Elkalkuli atributvalorojn</h3>
<p>Estas diversaj metodoj por elkalkuli atributvalorojn. Ĉiu el tiuj metodoj ebligas malsaman gradon de fleksebleco kaj hazardeco de la kreado de roluloj. Apliku la popolmodifilojn (vidu <a href="../popoloj">popoloj</a>) post elkalkulo de atributvaloroj. </p>
<p><b>Klasika:</b> Ĵetu 3k6 kaj sumigu la rezultojn de kubetoj. Notu la rezulton kaj ripetu tion ĝis vi havas ses valorojn. Ordigu tiujn valorojn al atributoj laŭ via elekto. Tiu metodo estas tre hazarda kaj kelkaj roluloj havos tre superajn atributojn. Tiu hazardeco povas esti eĉ pli granda, se vi ordigas la rezultojn al la atributoj laŭvice. Roluloj kiuj estas kreataj tiamaniere nur povas esti kreataj post la elkalkulo de atributvaloroj. </p>
<p><b>Normala:</b> Ĵetu 4k6 kaj sumigu la tri plej altajn rezultojn. Notu la rezulton kaj ripetu tion sesfoje. Ordigu la rezultojn al atributoj laŭ via prefero. Tiu metodo estas malpli hazarda ol la klasika. Roluloj kreitaj per tiu metodo inklinas al supermezkvantaj atributoj. </p>
<p><b>Heroa:</b> Ĵetu 2k6 kaj aldonu 6 al la rezultoj de kubetoj. Notu la rezulton kaj ripetu tion ĝis vi havas ses valorojn. Ordigu la rezultojn al atributoj laŭ via prefero. Tiu metodo kreas rolulojn kun plejparte supermezkvantaj atributoj. </p>

