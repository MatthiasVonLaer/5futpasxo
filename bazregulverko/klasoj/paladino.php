<h1>Paladino</h1>

<p><b>Rolo:</b> Paladinoj servas al siaj aliancanoj kiel lumturo en la ĥaoso de la batalo. Ili povas esti mortigaj kontraŭuloj de la malbono sed ankaŭ doni al bonaj estaĵoj la potencon por subteni ilin. Per siaj magiaj kaj militaj eblecoj ili fariĝas kompetentaj defendantoj kaj kiu falas al tiu ili donas la forton por ekstari kaj daŭrigi la batalon. </p>
