<title>Popoloj</title>

<h1>Popoloj</h1>

<h2>Duonelfoj</h2>
<ul>
    <li><b>+2 por unu atributo:</b> Pro la diverseco de siaj naturoj, duonelfoj ricevas bonon de +2 al iu atributo dum la kreado de la rolulo.</li>
    <li><b>Mezgranda:</b> Duonelfoj estas mezgrandaj kaj havas nek bonon nek malbonon pro sia grandeco.</li>
    <li><b>Normala moviĝorapideco:</b> Duonelfoj havas bazmoviĝorapidecon de 9 metroj.</li>
    <li><b>Krepuskovido:</b> Duonelfoj povas vidi en malhela lumo duoble pli malproksime ol homoj.</li>
    <li><b>Subtilaj sensoj:</b> Duonelfoj havas popolbonon de +2 por perceptĵetoj.</li>
    <li><b>Elfa imuneco:</b> Duonelfoj estas imunaj kontraŭ magiaj endormigefektoj kaj havas popolbonon de +2 por savĵetoj kontraŭ sorĉoj aŭ magiaj efikoj de la skolo transsorĉado.</li>
    <li><b>Adaptiĝemo:</b> Kiel kroma talento duonelfoj ricevas je la unua nivelo la talenton kapablecofokuson.</li>
    <li><b>Elfosango:</b> Por ĉiu efiko kiu rilatas al popolo, duonelfoj estas traktataj kaj kiel elfoj kaj kiel homoj.</li>
    <li><b>Multtalento:</b> Duonelfoj povas elekti du preferatajn klasojn je la unua nivelo kaj ricevas aŭ +1 vivpoenton aŭ +1 kapablecorangon, kiam ili altiĝas en unu el tiuj klasoj.</li>
    <li><b>Lingvoj:</b> Je la ludkomenco duonelfoj parolas la komunan lingvon kaj la elfan. Elfoj kun alta inteligenteco povas ankaŭ elekti ĉiun alian lingvon (krom sekretaj lingvoj kiel la druidan).</li>
</ul>

<h2>Duonorkoj</h2>
<ul>
    <li><b>+2 por unu atributo:</b> Duonorkoj ricevas dum la kreado de la rolulo bonon de +2 por iu atributo. Tiu reprezentas la diversecon de tiu popolo.</li>
    <li><b>Mezgranda:</b> Duonorkoj estas mezgrandaj kaj havas nek bonon nek malbonon pro sia grandeco.</li>
    <li><b>Normala moviĝorapideco:</b> Duonorkoj havas bazmoviĝorapidecon de 9 metroj.</li>
    <li><b>Mallumovido:</b> Duonorkoj povas vidi en mallumo ĝis 18 metroj.</li>
    <li><b>Sperto kun armiloj:</b> Duonorkoj estas spertuloj por falkatoj kaj labrisoj kaj ili uzas ĉiujn armilojn kiuj estas nomitaj "orkaj" kiel militarmiloj.</li>
    <li><b>Orka sovaĝeco:</b> Unufoje tage, se duonorko estas reduktita sub 0 vivpoentoj, sen morti, li povas daŭrigi tiel kiel li nur estus nebatalkapabla. Se li ne rehavas pli ol 0 vivpoentoj je la komenco de sia venonta vico, li subite estas senkonscia kaj komencas mortadi.</li>
    <li><b>Orksango:</b> Por ĉiu efiko kiu rilatas al popolo, duonorkoj estas traktataj kaj kiel orkoj kaj kiel homoj.</li>
    <li><b>Timiga:</b> Duonorkoj havas popolbonon de +2 por timigĵetoj ĉar ili ja faras timigan impreson.</li>
    <li><b>Lingvoj:</b> Je la ludkomenco duonorkoj parolas la komunan lingvon kaj la orkan. Elfoj kun alta inteligenteco povas ankaŭ inter la sekvantaj lingvoj: draka, giganta, gnola, goblina, abisa.</li>
</ul>

<h2>Elfoj</h2>
<ul>
    <li><b>+2 Lerteco, +2 Inteligenteco, -2 Konstitucio:</b> Elfoj estas mense kiel fizike viglaj, tamen ili estas de delikata figuro.</li>
    <li><b>Mezgranda:</b> Elfoj estas mezgrandaj kaj havas nek bonon nek malbonon pro sia grandeco.</li>
    <li><b>Normala moviĝorapideco:</b> Elfoj havas bazmoviĝorapidecon de 9 metroj.</li>
    <li><b>Krepuskovido:</b> Elfoj povas vidi en malhela lumo duoble pli malproksime ol homoj.</li>
    <li><b>Subtilaj sensoj:</b> Elfoj havas popolbonon de +2 por perceptĵetoj.</li>
    <li><b>Elfa imuneco:</b> Elfoj estas imunaj kontraŭ magiaj endormigefektoj kaj havas popolbonon de +2 por savĵetoj kontraŭ sorĉoj aŭ magiaj efikoj de la skolo transsorĉado.</li>
    <li><b>Elfa magio:</b> Elfoj havas popolbonon de +2 por sorĉnivelĵetoj por superi sorĉreziston. Krome ili havas popolbonon de +2 por tiuj sorĉkonoĵetoj kiuj servas por determini ecojn de magiaj objektoj.</li>
    <li><b>Sperto kun armiloj:</b> Elfoj estas spertuloj por longaj arkoj (ankaŭ kunmetitaj), longaj glavoj, rapiroj kaj kurtaj arkoj (ankaŭ kunmetitaj) kaj ili uzas ĉiujn armilojn kiuj estas nomitaj "elfaj" kiel militarmiloj.</li>
    <li><b>Lingvoj:</b> Je la ludkomenco elfoj parolas la komunan lingvon kaj la elfan. Elfoj kun alta inteligenteco povas ankaŭ elekti inter la sekvantaj lingvoj: draka, gnoma, goblina, orka, celestia, gnola, silvana.</li>
</ul>

<h2>Gnomoj</h2>
<ul>
    <li><b>+2 Konstitucio, +2 Karismo, -2 Forto:</b> Gnomoj estas fizike malfortaj, tamen mirige persistemaj, kaj en ilia sinteno manifestas ilia alloga karaktero.</li>
    <li><b>Malgranda:</b> Gnomoj estas malgrandaj, pro tio ili havas grandecbonon de +1 por sia armaĵklaso <i>(AK)</i>, grandecbonon de +1 por atakĵetoj kaj grandecbonon de +4 por sekretĵetoj. Tamen ili havas malbonon de -1 por sia batalmanovra bono <i>(BMB)</i> kaj por sia batalmanovra defendo <i>(BMD)</i>.
    <li><b>Malrapida:</b> Gnomoj havas bazmoviĝorapidecon de 6 metroj.</li>
    <li><b>Krepuskovido:</b> Gnomoj povas vidi en malhela lumo duoble pli malproksime ol homoj.</li>
    <li><b>Subtilaj sensoj:</b> Gnomoj havas popolbonon de +2 por perceptĵetoj.</li>
    <li><b>Diligenta:</b> Gnomoj havas popolbonon de +2 por unu metio aŭ profesio je via elekto.</li>
    <li><b>Iluzirezisto:</b> Gnomoj havas popolbonon de +2 por savĵetoj kontraŭ iluziefektoj.</li>
    <li><b>Gnoma magio:</b> Gnomoj altigas la gradon de malfacileco je +1 por savĵetoj kontraŭ iliaj iluzisorĉoj. Gnomoj kun karismo 11 aŭ pli havas krome la sekvantajn sorĉsimilajn eblecojn: 1/tago - <i>dancantaj lumoj</i>, <i>fantoma sono</i>, <i>paroli kun bestoj</i>, <i>sorĉartifiko</i>. La sorĉnivelo por tiuj efikoj egalas al la rolulnivelo de la gnomo. La grado de malfacileco por savĵetoj kontraŭ tiuj sorĉoj egalas al 10 + sorĉgrado + karismomodifilo de la gnomo.</li>
    <li><b>Sperto kun armiloj:</b> Gnomoj uzas ĉiujn armilojn kiuj estas nomitaj "gnomaj" kiel militarmiloj.</li>
    <li><b>Malamo:</b> Pro specifa edukado gnomoj havas bonon de +1 por atakĵetoj kontraŭ kreitaĵoj kiuj apartenas al subspeco reptilio aŭ goblino.</li>
    <li><b>Defendotrejniĝo:</b> Kontraŭ atakoj de monstroj de la speco gigantoj, gnomoj havas evitobonon de +4 por sia armaĵklaso.</li>
    <li><b>Lingvoj:</b> Je la ludkomenco gnomoj parolas la komunan lingvon, la gnoman kaj la silvanan. Gnomoj kun alta inteligenteco povas ankaŭ elekti inter la sekvantaj lingvoj: draka, elfa, giganta, goblina, nana, orka.</li>
</ul>

<h2>Hobitoj</h2>
<ul>
    <li><b>+2 Lerteco, +2 Karismo, -2 Forto:</b> Hobitoj estas lertmovaj kaj fortvolaj, tamen ilia malgranda staturo faras ilin malpli fortaj ol aliaj popoloj.</li>
    <li><b>Malgranda:</b> Hobitoj estas malgrandaj, pro tio ili havas grandecbonon de +1 por sia armaĵklaso <i>(AK)</i>, grandecbonon de +1 por atakĵetoj kaj grandecbonon de +4 por sekretĵetoj. Tamen ili havas malbonon de -1 por sia batalmanovra bono <i>(BMB)</i> kaj por sia batalmanovra defendo <i>(BMD)</i>.
    <li><b>Malrapida:</b> Hobitoj havas bazmoviĝorapidecon de 6 metroj.</li>
    <li><b>Subtilaj sensoj:</b> Hobitoj havas popolbonon de +2 por perceptĵetoj.</li>
    <li><b>Lertmova:</b> Hobitoj havas popolbonon de +2 por akrobataĵo- kaj grimpĵetoj.</li>
    <li><b>Hobitbonŝanco:</b> Hobitoj havas popolbonon de +1 por ĉiuj savĵetoj.</li> 
    <li><b>Sentima:</b> Hobitoj havas popolbonon de +2 por ĉiuj savĵetoj kontraŭ timo. Tiu bono sumiĝas al hobitbonŝanco.</li>
    <li><b>Sperto kun armiloj:</b> Hobitoj estas spertuloj por ŝtonĵetiloj kaj ili uzas ĉiujn armilojn kiuj estas nomitaj "hobitaj" kiel militarmiloj.</li>
    <li><b>Lingvoj:</b> Je la ludkomenco hobitoj parolas la komunan lingvon kaj la hobitan. Hobitoj kun alta inteligenteco povas ankaŭ elekti inter la sekvantaj lingvoj: elfa, gnoma, goblina kaj nana.</li>
</ul>

<h2>Homoj</h2>
<ul>
    <li><b>+2 por unu atributo:</b> Pro la diverseco de siaj naturoj, homoj ricevas bonon de +2 al iu atributo dum la kreado de la rolulo.</li>
    <li><b>Mezgranda:</b> Homoj estas mezgrandaj kaj havas nek bonon nek malbonon pro sia grandeco.</li>
    <li><b>Normala moviĝorapideco:</b> Homoj havas bazmoviĝorapidecon de 9 metroj.</li>
    <li><b>Kroma talento:</b> Homoj elektas unu plian talenton je la unua nivelo.</li>
    <li><b>Trejnita:</b> Homoj ricevas unu plian kapablecrangon je la unua nivelo kaj unu plian kapablecrangon dum ĉiu nivelaltiĝo.</li>
    <li><b>Lingvoj:</b> Je la ludkomenco homoj parolas la komunan lingvon. Homoj kun alta inteligenteco povas ankaŭ elekti ĉiun alian lingvon (krom sekretaj lingvoj kiel la druidan).</li>
</ul>


<h2>Nanoj</h2>
<ul>
    <li><b>+2 Konstitucio, +2 Saĝeco, -2 Karismo:</b> Nanoj estas tiel persistemaj kiel saĝaj, tamen iomete bruska interrilate kun aliaj.</li>
    <li><b>Mezgranda:</b> Nanoj estas mezgrandaj kaj havas nek bonojn nek malbonojn pro sia grandeco.</li>
    <li><b>Malrapida sed fortika:</b> Nanoj havas bazmoviĝorapidecon de 6 metroj, tamen ilia moviĝorapideco ne ŝanĝiĝas pro ŝarĝo aŭ armaĵo.</li>
    <li><b>Mallumovido:</b> Nanoj povas vidi en mallumo ĝis 18 metroj.</li>
    <li><b>Ŝtonsensiveco:</b> Nanoj havas bonon de +2 por perceptĵetoj por rekoni malkutimajn ŝtonmurojn aŭ masonaĵojn, kiel kaptiloj aŭ sekretpordoj kiuj troviĝas en ŝtonaj muroj aŭ plankoj. Ili rajtas fari perceptĵeton ĉiam kiam ili eniras distancon de 3 metroj, eĉ se ili ne estas serĉantaj.</li>
    <li><b>Avido:</b> Nanoj havas popolbonon de +2 por taksoĵetoj por eltrovi la prezon de nemagiaj produktoj, kiuj enhavas valorajn metalojn aŭ ŝtonojn.</li>
    <li><b>Hardita:</b> Nanoj havas popolbonon de +2 por savĵetoj kontraŭ venenoj, sorĉoj kaj sorĉsimilaj eblecoj.</li>
    <li><b>Sperto kun armiloj:</b> Nanoj estas spertuloj por batalhakiloj, pezaj pioĉoj kaj militaj marteloj. Ili uzas ĉiujn armilojn kiuj estas nomitaj "nanaj", kiel militarmiloj.</li>
    <li><b>Malamo:</b> Pro specifa edukado nanoj havas bonon de +1 por atakĵetoj kontraŭ kreitaĵoj kiuj apartenas al subspeco de orkoj aŭ goblinoj.</li>
    <li><b>Defendotrejniĝo:</b> Kontraŭ atakoj de monstroj de la speco giganto, nanoj havas evitobonon de +4 por sia armaĵklaso.</li>
    <li><b>Necedema:</b> Nanoj havas popolbonon de +4 por sia batalmanovra defendo, se ili volas kontraŭstari puŝatakon aŭ atakon de faligi, se ili staras sur firma grundo.</li>
    <li><b>Lingvoj:</b> Je la ludkomenco nanoj parolas la komunan lingvon kaj la nanan. Nanoj kun alta inteligenteco povas ankaŭ elekti inter la sekvantaj lingvoj: giganta, gnoma, goblina, orka, terana, subkomuna.
</ul>

