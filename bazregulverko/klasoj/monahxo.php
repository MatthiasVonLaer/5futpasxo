<p> La monaĥo kaŭzas per sia senarma atako pli da damaĝo ol aliaj batalistoj kiel troveblas en la tabelo "Monaĥo". La indikoj validas por mezgranda monaĥo. Malgranda monaĥo kaŭzas malpli kaj granda kaŭzas pli da damaĝo. </p>

<p id="kroma-talento"><b>Kroma talento:</b> Je la 1a kaj 2a nivelo kaj poste je ĉiu kvara nivelo, la monaĥo rajtas elekti kroman talenton. Li devas elekti la talenton el la sekvanta listo: batalrefleksoj, evitmovo, luktado progresita, preterdirekti pafaĵon, improviza malproksimatako, improviza proksimatako, skorpia pikilo. Je la 6a nivelo aldoniĝas la ponstaj talentoj: faligi progresita, finto progresita, gorgona pugno, movebleco, senarmigi progresita, puŝatako progresita. Je la 10a nivelo aldoniĝas la sekvantaj talentoj: dancanta atako, kapti pafaĵon, meduza kolero, kritika trafo progresita. La monaĥo ne devas plenumi la kondiĉojn de la talentoj. </p>

<p id="sveniga-bato"><b>Sveniga bato (EE):</b> Je la 1a nivelo la monaĥo ricevas svenigan baton kiel kroma talento ankaŭ se li ne plenumas la kondiĉojn. Je la 4a nivelo kaj je ĉiu plia kvara nivelo la monaĥo ricevas novan staton kiun li povas kaŭzi al la celo de la sveniga bato. Tiu stato anstataŭigas la la svenigo de la celo. La celo povas savi sin kontraŭ tiu stato per savĵeto. Ekde la 4a nivelo li povas elekti ke la celo estu elĉerpita. Ekde la 8a nivelo li povas malsanetigi la celon. Ekde la 12a nivelo povas elekti ke la celo estu ŝanceliĝa dum 1k6+1 vicoj. Ekde la 16a nivelo li povas elekti ke la celo daŭre blindiĝas aŭ surdiĝas. Je la 20a nivelo li povas elekti ke la celo estu paralizata dum 1k6+1 vicoj. La monaĥo devas decidi antaŭ la atakoĵeto kiun staton li volas kaŭzi. Tiuj efikoj ne sumiĝas (tio signifas, ke estaĵo kiu jam svenas ne naŭzas pro plia sveniga bato), sed pliaj trafoj plilongigas la daŭron de la stato. </p>

<p id="sinsavi"><b>Sinsavi (EE):</b> Se monaĥo, kiu estas minimume de la 2a nivelo, faras refeksĵeton por ricevi nur duonan damaĝon, li ricevas neniun damaĝon se li sukcesas. La monaĥo nur povas sinsavi se li portas nur malpezan aŭ ne portas armaĵon. Monaĥo kiu estas senkapabla ne povas uzi tiun eblecon. </p>

<p id="rapida-movigxo"><b>Rapida moviĝo:</b> Ekde la 3a nivelo la monaĥo ricevas laŭ la tabelo "Monaĥo" bonon por sia moviĝorapideco. Monaĥo kiu portas armaĵon aŭ pli ol malpezan ŝarĝon, perdas tiun bonon.</p>

<p id="manovrotrejnado"><b>Manovrotrejnado:</b> Ekde la 3a nivelo la monaĥo uzas sian nivelon kiel bazataka bono por determini sian batalmanovran bonon (ne koncernas la batalmanovran defendon). La bazataka bono kiun li havas de aliaj klasoj ne ŝanĝiĝas kaj sumiĝas en kutima maniero. </p>

<p id="trankvila-spirito"><b>Trankvila spirito:</b> Je la 3a nivelo la monaĥo ricevas bonon de +2 por savĵetoj kontraŭ sorĉoj kaj efikoj de la sorĉskolo transsorĉado.</p>

<p id="ki-provizo"><b><em>Ki</em>-provizo (SNE):</b> Je la 4a nivelo la monaĥo ricevas provizon da <em>ki</em>-poentoj, tipo da supernatura energio, per kio li povas efektivigi impresajn agojn. La kvano da poentoj en lia <em>ki</em>-provizo egala al lia duona nivelo plus lia SAĜ-modifilo. Tiel longe kiel li havas minimume unu poenton en sia provizon, li povas fari <em>ki</em>-batojn. Je la 4a nivelo lia senarma atako kalkuliĝas, dank' al <em>ki</em>-bato, kiel magia armilo por superi damaĝoredukton. La <em>ki</em>-bato pliboniĝas kiam la monaĥo altiĝas je nivelo. Ekde la 10a nivelo la senarmaj atakoj de la moanĥo kalkuliĝas por superi damaĝoredukton kiel laŭleĝa armilo. Kaj ekde la 16a nivelo ili kalkuliĝas por superi damaĝoredukton kiel adamanta armilo. </p>

<p> Se la monaĥo uzas la atakhajlon, li povas eluzi unu <em>ki</em>-poenton el sia provizon por fari plian atakon per sia plej alta atakbono. Krome li povas uzi unu <em>ki</em>-poenton por altigi sian moviĝorapidecon je 6 metroj dum unu vico. Por unu <em>ki</em>-poento li ankaŭ povas ricevi evitbonon de +4 por sia armaĵklaso. Por efektivigi unu tian eblecon la monaĥo povas uzi rapidan agon. La monaĥo ricevas pliajn eblecojn por siaj <em>ki</em>-poentoj kiam li altiĝaas. </p>

<p> La <em>ki</em>-provizo refreŝiĝas post ok horoj paŭzo aŭ meditado. Tiuj ok horoj ne devas esti dum unu sinsekvo. </p>

<p id="bremsi-falon"><b>Bremsi falon (EE):</b> Monaĥo kiu alvenis minimume al la 4a nivelo kaj kiu ne estas pli distanca al muro ol la longeco de sia brako, povas uzi ĝin por bremsi falon. Kiam li ricevas la eblecon, la falalteco por kalkuli la damaĝon reduktiĝas je 6 metroj. Kiam li plibonigas siajn klasnivelojn tiu kvanto altiĝas ĝis la 20a nivelo kiam li povas bremsi ĉiun falon proksime al muro. </p>

<p id="altosalto"><b>Altosalto (EE):</b> Je la 5a nivelo la monaĥo aldonas sian nivelon al ĉiuj kapablecĵetoj de akrobataĵo por salti horizontale aŭ vertikale. Plie li ĉiam povas supozi ke li havus impetdonan alkuron por ĵetoj de akrobataĵo. Per eluzo de unu <em>ki</em>-poento la monaĥo ricevas per rapida ago bonon de +20 por sian ĵeton de akrobataĵo por salti. </p>

<p id="pureco-de-la-korpo"><b>Pureco de la korpo (EE):</b> Ekde la 5a nivelo la monaĥo estas imuna kontraŭ ĉiuj malsanoj ankaŭ supernaturaj kaj magiaj. </p>

<p id="integreco-de-la-korpo"><b>Integreco de la korpo (EE):</b> Ekde la 7a nivelo la monaĥo povas sanigi siajn vundojn per bazago. Li devas eluzi du <em>ki</em>-pontojn kaj tiam povas sanigi kvanton da vivpoentoj kiuj egalas al sia nivelo. </p>

<p id="sinsavi-progresita"><b>Sinsavi progresite (EE):</b> Ekde la 9a nivelo pliboniĝas la ebleco sinsavi de atakoj. Se la monaĥo estas atakata tiel ke li rajtas fari refleksĵeton por duonigi la damaĝon, tiam li ricevas - kiel ĝis nun - neniun damaĝon se li sukcesas. Sed se li malsukcesas li ricevas nur duonon de la damaĝo. Monaĥo kiu estas senkapabla ne povas uzi tiun eblecon. </p>

<p id="diamantkorpo"><b>Diamantokorpo (SNE):</b> Je la 11a nivelo la monaĥo ricevas imunecon kontraŭ ĉiuj tipoj da venenoj. </p>

<p id="abunda-pasxo"><b>Abunda paŝo (SNE):</b> Je la 12a nivelo la monaĥo scipovas moviĝi inter dimensioj tiel kiel per la sorĉo dimensiopordo. Por uzi tiun eblecon la monaĥo evas eluzi du <em>ki</em>-poentojn. Kiel sorĉnivelo kalkuliĝas la klasnivelo de la monaĥo. Li ne povas kunpreni aliajn estaĵojn se li uzas tiun eblecon. </p>

<p id="diamantanimo"><b>Diamantanimo (EE):</b> Je la 13a nivelo la monaĥo ricevas sorĉreziston de 10 + sia nuna klasnivelo. Por efektivigi sorĉon al la monaĥo, la sorĉanto devas atingi aŭ superi la sorĉreziston de la monaĥo per sorĉnivelprovon (1k20 + sorĉnivelo). </p>

<p id="vibranta-manplato"><b>Vibranta manplato (SNE):</b> Ekde la 15a nivelo la monaĥo povas vibrigi la korpon de kontraŭulo, kiuj poste estos mortigaj se la monaĥo deziros tion. Li povas uzi la vibrantan manplaton unufoje tage kaj devas anonci tion antaŭ sia atakĵeto. Kreaĵoj kiuj ne povas esti atakataj per kritikaj trafoj estas imunaj kontraŭ tiu ebleco. Alie se la monaĥo trafas kaj kaŭzas damaĝon, la ebleco sukcese efektiviĝis. </p>

<p> Post la trafo la monaĥo povas provi, dum nombro da tagoj kiu egalas al sia klasnivelo, mortigi la viktimon. La monaĥo nur devas deziri la morton de la celo (kiel libera ago). Se la viktimo ne sukcese faras persistecĵeton (MFG: 10 + &frac12;&nbsp;monaĥonivelo + SAĜ-modifilo de la monaĥo), li mortas. Se la savĵeto sukcesas, la viktimo ne plu estas en danĝero de la vibranta manplato. Tamen poste li povas esti denove la celo de tia atako. La monaĥo ne povas havi pli ol unu aktivan manplaton samtempe. Se li uzas vibrantan manplaton dum alia apliko ankoraŭ efikadas, tiu pli frua efiko finiĝas. </p>

<p id="sentempa-korpo"><b>Sentempa korpo (EE):</b> Ekde la alveno al la 17a nivelo la atributoj de la monaĥo ne plu malboniĝas rilate al maljuniĝo. Li ankaŭ ne povas maljuniĝi pro magiaj efikoj. Ŝanĝoj kiuj okazis antaŭe ne reŝanĝas. Bonoj pro la aĝo li tamen ricevas kaj li mortas se la tempo venas. </p>

<p id="lingvo-de-luno-kaj-suno"><b>Lingvo de luno kaj suno (EE):</b> Monaĥo de la 17a aŭ pli alta nivelo scipovas paroli la lingvon de ĉiu vivanta kreaĵo. </p>

<p id="malplena-korpo"><b>Malplena korpo (SNE):</b> Je la alveno al la 19a nivelo la monaĥo ricevas la eblecon iri al la etera ebeno dum unu minuto tiel kiel per la sorĉo eteraj staturoj. Uzi la eblecon kostas unu movagon kaj tri <em>ki</em>-poentojn. Tiu ebleco nur koncernas la monaĥon kaj ne povas esti aplikata por kunpreni alian personon. </p>

<p id="perfekta-memeco"><b>Perfekta memeco:</b> Je la 20a nivelo la monaĥo iĝas magia estaĵo. Ekde tiam li kalkuliĝas por sorĉoj kaj magiaj efikoj ne plu kiel humanoida (aŭ de kiu specio la monaĥo estis antaŭe) sed kiel eksterulo. Plie li ricevas damaĝoredukton de 10/ĥaosa. Kun tio li povas subtrahi 10 poentojn de la damaĝo de ĉiu atako kiu ne estas de ĥaosa armilo aŭ de estaĵo de la subkategorio ĥaosa. </p>

<p> Alie ol kutimaj eksteruloj, la monaĥo povas esti revivigita el la mortoj kiel konformas al sia antaŭa specio. </p>

