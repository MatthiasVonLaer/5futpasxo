    <li><b>Malrapida sed fortika:</b> Nanoj havas bazmoviĝrapidecon de 6 metroj, tamen ilia moviĝrapideco ne ŝanĝiĝas pro ŝarĝo aŭ armaĵo.</li>
    <li><b>Mallumovido:</b> Nanoj povas vidi en mallumo ĝis 18 metroj.</li>
