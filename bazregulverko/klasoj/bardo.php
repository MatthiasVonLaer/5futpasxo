<h1>Bardo</h1>

<p><b>Rolo:</b> Bardoj spertas influi aliajn kreaĵojn kaj konfuzi ilin, samtempe ili inspiras al siaj kamaradoj kuraĝon kaj bravecon. Unuflanke ili atingas tion helpe de siaj armiloj kaj per magio. La vera forto de la bardo tamen ne estas la proksimbatalo, sed ilia ebleco subteni siajn kamaradojn kaj malhelpi siajn malamikojn sen peni por daŭrigi la prezentadon. </p>
<p><b>Pensmaniero:</b> ĉiu</p>
